/// POST /api/sync-kicad-pcb/:name — file-based KiCad sync. Reads the
/// `.kicad_pcb` declared by the design's `(kicad-pcb "<path>")` form,
/// computes the diff against the design's flattened netlist, and applies
/// the ops to the file in place.
/// Query flags: `?dry_run=1` returns the JSON envelope the diff would
/// produce without writing; `?migrate=1` enables the heuristic relink
/// (parent-path + value + net signature) to recover a board whose
/// canopy_uuids/ref-des drifted; `?prune=1` turns every `flag_stale` op
/// into a real `remove`; `?dot_nets=1` keeps the per-pin `(decouple …)`
/// sub-nets (`VDD.U18.IN`) as pad nets instead of collapsing to the rail,
/// so each bypass cap shows which pin it belongs to; `?refresh=1` re-bakes
/// every matched part's footprint geometry (same-name swap) so an existing
/// board backfills new silkscreen/fab outlines without moving anything.
pub fn syncKicadPcbApi(ctx: *Handler, req: *httpz.Request, res: *httpz.Response) HandlerError!void {
    const name = req.param("name") orelse {
        res.status = HTTP_NOT_FOUND;
        return;
    };
    const dry_run = isDryRun(req);
    const prune_stale = isQueryFlagSet(req, "prune");
    // `?migrate=1` enables the heuristic relink (parent-path + value + net
    // signature) so a board whose canopy_uuids/ref-des drifted from the
    // design gets re-linked in place (placement preserved) instead of
    // churning into add+stale. The conservative default stays off.
    const migrate = isQueryFlagSet(req, "migrate");
    // `?dot_nets=1` keeps each decoupling cap on its own per-pin sub-net.
    const dot_nets = isQueryFlagSet(req, "dot_nets");
    // `?refresh=1` re-bakes every matched part's footprint geometry (same-name
    // swap) so a board backfills new silkscreen/fab without moving anything.
    const refresh = isQueryFlagSet(req, "refresh");
    runKicadPcbSync(ctx, req, res, name, dry_run, prune_stale, migrate, dot_nets, refresh) catch |err| switch (err) {
        error.PcbPathUnset => sendError(res, HTTP_BAD_REQUEST, ERR_NO_PCB_PATH),
        error.PcbReadFailed => sendError(res, HTTP_INTERNAL_ERROR, ERR_PCB_READ_FAILED),
        error.PcbParseFailed => sendError(res, HTTP_INTERNAL_ERROR, ERR_PCB_PARSE_FAILED),
        error.PcbWriteFailed => sendError(res, HTTP_INTERNAL_ERROR, "failed to write the updated .kicad_pcb to disk"),
        error.WriterFailed => sendError(res, HTTP_INTERNAL_ERROR, "failed to apply ops to the .kicad_pcb (writer error)"),
        error.EnvelopeParseFailed => sendError(res, HTTP_INTERNAL_ERROR, "internal: failed to parse sync-plan envelope"),
        error.BuildFailed => sendError(res, HTTP_INTERNAL_ERROR, ERR_BUILD_ERROR),
        error.NotADesign => sendError(res, HTTP_INTERNAL_ERROR, ERR_NOT_A_DESIGN),
        else => |e| return e,
    };
}

const KicadPcbSyncError = error{
    PcbPathUnset,
    PcbReadFailed,
    PcbParseFailed,
    PcbWriteFailed,
    WriterFailed,
    EnvelopeParseFailed,
} || SyncRunError;

fn sendError(res: *httpz.Response, status: u16, body: []const u8) void {
    res.status = status;
    res.body = body;
}

/// Inner implementation of `syncKicadPcbApi` — uses errors instead of
/// inline HTTP-response writes so the wrapper can stay under Guardian's
/// returns-per-function cap. The wrapper translates each error variant
/// to an HTTP status + body.
fn runKicadPcbSync(
    ctx: *Handler,
    req: *httpz.Request,
    res: *httpz.Response,
    name: []const u8,
    dry_run: bool,
    prune_stale: bool,
    migrate: bool,
    dot_nets: bool,
    refresh: bool,
) KicadPcbSyncError!void {
    // Resolve the design and its declared PCB path.
    const board_path = try paths.designSourcePath(req.arena, ctx.project_dir, name);
    var eval = Evaluator.init(ctx.allocator, ctx.project_dir);
    defer eval.deinit();
    const result_val = eval.evalFile(board_path) catch return error.BuildFailed;
    const block = switch (result_val) {
        .design_block => |b| b,
        else => return error.NotADesign,
    };
    const pcb_path = block.kicad_pcb_path orelse return error.PcbPathUnset;

    // Read the PCB file from disk. Absolute paths (NAS mounts) are the
    // expected form; relative paths resolve from the server's CWD.
    const src = infra_fs.cwd().readFileAlloc(req.arena, pcb_path, MAX_PCB_BYTES) catch return error.PcbReadFailed;

    const board = kicad_pcb_reader.readBoard(req.arena, src) catch return error.PcbParseFailed;

    // Run the diff. `migrate_heuristic` defaults off for the file-based
    // path; `prune_stale` is opt-in via `?prune=1` from the
    // "Push + Delete Stale" button — it flips every `flag_stale` op in
    // the diff into a real `remove` so orphan footprints (no longer in
    // the design) get dropped from the file in one shot.
    const plan: ParsedSyncPlan = .{
        .board = board,
        .prune_stale = prune_stale,
        .migrate_heuristic = migrate,
        .dot_nets = dot_nets,
        .refresh = refresh,
        .applied_ops = std.StringHashMap(void).init(req.arena),
    };
    const run = try runSyncPlan(req.arena, ctx.allocator, ctx.project_dir, name, plan);

    // Dry-run mode (?dry_run=1) returns the would-be ops without
    // writing — useful for the agent-parity comparison and as a
    // pre-flight from the UI.
    if (dry_run) {
        res.content_type = .JSON;
        res.header(HEADER_CORS_ALLOW_ORIGIN, "*");
        res.body = run.body;
        return;
    }

    const ops_json = extractOpsArrayJson(run.body) orelse return error.EnvelopeParseFailed;

    var stats: kicad_pcb_writer.ApplyStats = .{};
    const new_pcb = kicad_pcb_writer.applyOpsToSourceWithStats(req.arena, src, ops_json, &stats) catch return error.WriterFailed;

    // Detect a sibling KiCad lockfile (`~<basename>.lck`). pcbnew writes
    // it while a board is open. Per the user's choice we write anyway
    // and surface the warning in the UI — KiCad 8+ pops "file modified
    // externally, reload?"; KiCad 7 needs File → Revert. The user must
    // act before saving in pcbnew or our write loses.
    const lock_warning = pcbnewLockMessage(req.arena, pcb_path);

    // Skip the disk write when nothing semantically changed. The writer's
    // helpers each report null for redundant ops (`set_field` with the
    // existing value, `set_pad_net` with the existing net, `add` for a
    // canopy_uuid already on the board, etc.); when every stat is zero
    // the AST is byte-identical to the input and rewriting would just
    // re-normalise whitespace and bump the mtime, making pcbnew prompt
    // "file modified, reload?" every push and reporting non-zero changes
    // to the user forever.
    const wrote_file = !statsAreZero(stats);
    if (wrote_file) writeFileAtomic(req.arena, pcb_path, new_pcb) catch return error.PcbWriteFailed;

    // Mirror the schematic source (the design `.sexp` + every sub-block module/
    // file it uses, recursively) into a read-only `<design>-source/` folder
    // beside the board, so the KiCad project folder carries a self-contained
    // snapshot of the source. Best-effort, opt-out via ?no_source_copy=1; a
    // failure here is logged and never fails the sync (the board write is done).
    const source_copied: usize = if (isQueryFlagSet(req, "no_source_copy"))
        0
    else
        copyDesignSources(req.arena, ctx.project_dir, name, board_path, block, pcb_path);

    // Ship the 3D models alongside the board. The sync bakes
    // `${KIPRJMOD}/models/<name>.step` into each footprint but the file-based
    // push historically never copied the files, so KiCad's 3D viewer resolved
    // nothing. Done unconditionally (even on a no-op netlist push) so an already
    // synced board still backfills a missing `models/` folder; idempotent and
    // opt-out via ?no_model_copy=1.
    const models_copied: usize = if (isQueryFlagSet(req, "no_model_copy"))
        0
    else
        copyKicadModels(req.arena, ctx.project_dir, block, pcb_path);

    var resp_buf: std.ArrayListUnmanaged(u8) = .empty;
    const rw = resp_buf.writer(ctx.allocator);
    try rw.print(
        "{{\"ok\":true,\"design_version\":{d}," ++
            "\"applied\":{{\"added\":{d},\"removed\":{d},\"swapped\":{d}," ++
            "\"fields_set\":{d},\"pad_nets_set\":{d},\"locked_changed\":{d}," ++
            "\"fields_hidden\":{d},\"fields_shown\":{d}}}",
        .{
            run.version,          stats.added,         stats.removed,
            stats.swapped,        stats.fields_set,    stats.pad_nets_set,
            stats.locked_changed, stats.fields_hidden, stats.fields_shown,
        },
    );
    try rw.print(",\"source_copied\":{d}", .{source_copied});
    try rw.print(",\"models_copied\":{d}", .{models_copied});
    if (lock_warning) |msg| {
        if (wrote_file) {
            try rw.writeAll(",\"warning\":");
            try json_writer.writeString(rw, msg);
        }
    }
    try rw.writeAll("}");
    res.content_type = .JSON;
    res.header(HEADER_CORS_ALLOW_ORIGIN, "*");
    res.body = resp_buf.items;
}

fn statsAreZero(s: kicad_pcb_writer.ApplyStats) bool {
    return s.added == 0 and s.removed == 0 and s.swapped == 0 and
        s.fields_set == 0 and s.pad_nets_set == 0 and s.locked_changed == 0 and
        s.fields_hidden == 0 and s.fields_shown == 0;
}

/// Returns a user-facing message when pcbnew has the target board open
/// (a `~<basename>.lck` sibling exists). Null when no lock is detected.
fn pcbnewLockMessage(arena: std.mem.Allocator, pcb_path: []const u8) ?[]const u8 {
    const basename = std.fs.path.basename(pcb_path);
    const dirname = std.fs.path.dirname(pcb_path) orelse return null;
    const lock_name = std.fmt.allocPrint(arena, "{s}/~{s}.lck", .{ dirname, basename }) catch return null;
    _ = infra_fs.cwd().statFile(lock_name) catch return null;
    return "pcbnew has the board open — File \u{2192} Revert (or accept the auto-reload prompt) before saving in pcbnew";
}

/// Honors `?dry_run=1` (or `=true`) on the request URL.
fn isDryRun(req: *httpz.Request) bool {
    return isQueryFlagSet(req, "dry_run");
}

/// True when the named query param is set to "1" or "true". Used for
/// the file-based KiCad sync's `?dry_run=1` and `?prune=1` toggles.
fn isQueryFlagSet(req: *httpz.Request, key: []const u8) bool {
    const q = req.query() catch return false;
    const v = q.get(key) orelse return false;
    return std.mem.eql(u8, v, "1") or std.mem.eql(u8, v, "true");
}

/// Strip the `{design_version, summary, ops: [...]}` envelope and return
/// the raw `[...]` ops array as JSON text. The writer's
/// `applyOpsToSourceWithStats` expects just the array, not the envelope.
fn extractOpsArrayJson(envelope: []const u8) ?[]const u8 {
    const ops_key = "\"ops\":";
    const idx = std.mem.indexOf(u8, envelope, ops_key) orelse return null;
    const start = idx + ops_key.len;
    if (start >= envelope.len or envelope[start] != '[') return null;
    // The envelope's `}` follows the closing `]` of the array, so trim it.
    const last_brace = std.mem.lastIndexOfScalar(u8, envelope, '}') orelse return null;
    if (last_brace <= start) return null;
    return envelope[start..last_brace];
}

/// Write `contents` to `path` atomically via tmp file → fsync(file) →
/// rename. NAS callers concerned about partial visibility through NFS
/// caches can run `sync` post-hoc; for a human-driven button press the
/// rename is durable enough in practice.
fn writeFileAtomic(arena: std.mem.Allocator, path: []const u8, contents: []const u8) !void {
    // Roll the current file to `<path>.bak` before overwriting so a bad
    // sync (e.g. an unwanted prune) is one copy away from undo — the board
    // lives on the NAS, outside git, so this is the only safety net. Abort
    // the whole write if the backup can't be made; better to fail loudly
    // than overwrite with no fallback. FileNotFound = first sync, nothing
    // to back up yet, so proceed.
    const backup_path = try std.fmt.allocPrint(arena, "{s}.bak", .{path});
    infra_fs.cwd().copyFile(path, infra_fs.cwd(), backup_path, .{}) catch |e| switch (e) {
        error.FileNotFound => {},
        else => return e,
    };

    const tmp_path = try std.fmt.allocPrint(arena, "{s}.tmp", .{path});

    var tmp = try infra_fs.cwd().createFile(tmp_path, .{ .truncate = true });
    {
        defer tmp.close();
        try tmp.writeAll(contents);
        // Best-effort durability before the rename. On systems where
        // fsync isn't supported (in-memory FS in some test sandboxes)
        // we still want the rename to proceed — surfacing the error
        // would make the whole sync fail for a non-actionable reason.
        tmp.sync() catch |e| log.warn("kicad-pcb tmp fsync failed: {s}", .{@errorName(e)});
    }

    try infra_fs.cwd().rename(tmp_path, path);
}

// ── Read-only source mirror beside the board ─────────────────────────
// On a real push, copy the design's `.sexp` + its sub-block module/file
// sources into `<board_dir>/<design>-source/` (mirroring the project's
// src/ + lib/ layout) as read-only files, so the KiCad project folder
// carries a self-contained snapshot of the schematic source.

/// Copy the design source + every sub-block module/file it uses (recursively)
/// into a read-only `<design>-source/` folder beside the `.kicad_pcb`. Returns
/// the count written or already up-to-date. Best-effort: any single failure is
/// logged and skipped. Components imported by a module are not pulled in — only
/// the design and its sub-blocks/sub-modules.
fn copyDesignSources(
    arena: std.mem.Allocator,
    project_dir: []const u8,
    design_name: []const u8,
    board_path: []const u8,
    block: *const env_mod.DesignBlock,
    pcb_path: []const u8,
) usize {
    const board_dir = std.fs.path.dirname(pcb_path) orelse ".";
    const dest_root = std.fmt.allocPrint(arena, "{s}/{s}{s}", .{ board_dir, design_name, SOURCE_DIR_SUFFIX }) catch return 0;

    var rels: std.ArrayListUnmanaged([]const u8) = .empty;
    var seen = std.StringHashMap(void).init(arena);
    // The design file itself: its read path is `<project_dir>/src/…`, so strip
    // the prefix to get the mirror-relative path.
    addRel(arena, &rels, &seen, projectRelative(board_path, project_dir));
    collectSubBlockSources(arena, &rels, &seen, block);

    var written: usize = 0;
    for (rels.items) |rel| {
        if (copyOneReadOnly(arena, project_dir, dest_root, rel)) written += 1;
    }
    return written;
}

/// Copy every 3D model (`.step`) referenced by the design into a `models/`
/// folder beside the board, so the `${KIPRJMOD}/models/<name>.step` paths the
/// sync bakes into each footprint actually resolve in KiCad's 3D viewer.
/// `${KIPRJMOD}` evaluates to the `.kicad_pcb`'s directory, so the files have to
/// live in `<board_dir>/models/`. Mirrors `export_kicad_model.exportFootprints`'
/// model-copy step (same model-config + `findModelFile` resolution), which is
/// why `export-kicad` boards always had their models while file-based pushes did
/// not. Best-effort: a failure is logged and never fails the sync. Returns the
/// number of model files written (skips those already present, so a repeat push
/// just stats each file — no NAS churn).
fn copyKicadModels(
    arena: std.mem.Allocator,
    project_dir: []const u8,
    block: *const env_mod.DesignBlock,
    pcb_path: []const u8,
) usize {
    const board_dir = std.fs.path.dirname(pcb_path) orelse ".";
    const models_dir = std.fmt.allocPrint(arena, "{s}/models", .{board_dir}) catch return 0;
    infra_fs.cwd().makePath(models_dir) catch |e| {
        log.warn("model-copy: mkdir {s} failed: {s}", .{ models_dir, @errorName(e) });
        return 0;
    };

    var instances: std.ArrayListUnmanaged(export_kicad.FlatInstance) = .empty;
    netlist_mod.collectInstances(arena, block, "", &instances) catch return 0;

    var model_cfg = export_kicad.loadModelConfig(arena, project_dir);
    var seen = std.StringHashMap(void).init(arena);

    var copied: usize = 0;
    for (instances.items) |inst| {
        if (inst.footprint.len == 0) continue;
        const mcfg = model_cfg.get(inst.footprint);
        const model_name = if (mcfg) |c|
            (c.model orelse fp_mod.findModelFile(arena, project_dir, inst.footprint, inst.component))
        else
            fp_mod.findModelFile(arena, project_dir, inst.footprint, inst.component);
        const mname = model_name orelse continue;
        if (seen.contains(mname)) continue;
        seen.put(mname, {}) catch |e| log.warn("model-copy: track {s} failed: {s}", .{ mname, @errorName(e) });
        if (copyOneModel(arena, project_dir, models_dir, mname)) copied += 1;
    }
    return copied;
}

/// Copy `lib/models/<mname>` into the board's `models/` dir. Skips silently when
/// the source model is missing (not every footprint ships a STEP) and when an
/// identical-size copy already exists (idempotent re-push). Returns true only
/// when a file was actually written.
fn copyOneModel(
    arena: std.mem.Allocator,
    project_dir: []const u8,
    models_dir: []const u8,
    mname: []const u8,
) bool {
    const src_path = std.fmt.allocPrint(arena, "{s}/lib/models/{s}", .{ project_dir, mname }) catch return false;
    const dst_path = std.fmt.allocPrint(arena, "{s}/{s}", .{ models_dir, mname }) catch return false;
    const src_stat = infra_fs.cwd().statFile(src_path) catch return false;
    if (infra_fs.cwd().statFile(dst_path)) |dst_stat| {
        if (dst_stat.size == src_stat.size) return false;
    } else |_| {}
    infra_fs.cwd().copyFile(src_path, infra_fs.cwd(), dst_path, .{}) catch |e| {
        log.warn("model-copy: {s} failed: {s}", .{ mname, @errorName(e) });
        return false;
    };
    return true;
}

/// Recursively gather each sub-block's source as a project-relative `.sexp`
/// path: a slashed `source` is already a project-relative file path; a bare
/// `source` is a module name under `lib/modules/`. Empty sources (sub-blocks
/// built without provenance) are skipped. Dedupes via `seen`.
fn collectSubBlockSources(
    arena: std.mem.Allocator,
    rels: *std.ArrayListUnmanaged([]const u8),
    seen: *std.StringHashMap(void),
    block: *const env_mod.DesignBlock,
) void {
    for (block.sub_blocks) |sb| {
        if (sb.source.len > 0) {
            const rel = if (std.mem.indexOfScalar(u8, sb.source, '/') != null)
                sb.source
            else
                std.fmt.allocPrint(arena, "lib/modules/{s}.sexp", .{sb.source}) catch sb.source;
            addRel(arena, rels, seen, rel);
        }
        collectSubBlockSources(arena, rels, seen, sb.block);
    }
}

/// Append `rel` to `rels` unless already present (dedupes a module reused by
/// several sub-blocks down to one copy).
fn addRel(
    arena: std.mem.Allocator,
    rels: *std.ArrayListUnmanaged([]const u8),
    seen: *std.StringHashMap(void),
    rel: []const u8,
) void {
    if (seen.contains(rel)) return;
    seen.put(rel, {}) catch return;
    rels.append(arena, rel) catch |e| log.warn("source-copy: track {s} failed: {s}", .{ rel, @errorName(e) });
}

/// Strip a leading `<project_dir>/` from a read path so the mirror keeps the
/// project's `src/…` / `lib/…` layout. Falls back to the basename for a path
/// that isn't under `project_dir` (e.g. an absolute one).
fn projectRelative(path: []const u8, project_dir: []const u8) []const u8 {
    if (project_dir.len > 0 and std.mem.startsWith(u8, path, project_dir) and
        path.len > project_dir.len and path[project_dir.len] == '/')
        return path[project_dir.len + 1 ..];
    return std.fs.path.basename(path);
}

/// Read `<project_dir>/<rel>`, prepend the provenance banner, and write it
/// read-only to `<dest_root>/<rel>`. Returns true on success or when the copy
/// already matches; logs and returns false on failure.
fn copyOneReadOnly(
    arena: std.mem.Allocator,
    project_dir: []const u8,
    dest_root: []const u8,
    rel: []const u8,
) bool {
    const read_path = std.fmt.allocPrint(arena, "{s}/{s}", .{ project_dir, rel }) catch return false;
    const content = infra_fs.cwd().readFileAlloc(arena, read_path, MAX_SOURCE_BYTES) catch |e| {
        log.warn("source-copy: read {s} failed: {s}", .{ read_path, @errorName(e) });
        return false;
    };
    const header = std.fmt.allocPrint(arena, SOURCE_HEADER_FMT, .{rel}) catch return false;
    const body = std.fmt.allocPrint(arena, "{s}{s}", .{ header, content }) catch return false;
    const dest_path = std.fmt.allocPrint(arena, "{s}/{s}", .{ dest_root, rel }) catch return false;
    return writeReadOnlyFile(arena, dest_path, body);
}

/// Atomically write `content` to `dest_path` read-only, creating parent dirs.
/// Skips when an identical copy already exists (no NAS churn). Uses tmp-then-
/// rename so a re-push replaces the existing read-only file (rename needs
/// directory write permission, not file write permission).
fn writeReadOnlyFile(arena: std.mem.Allocator, dest_path: []const u8, content: []const u8) bool {
    const dir = std.fs.path.dirname(dest_path) orelse return false;
    infra_fs.cwd().makePath(dir) catch |e| {
        log.warn("source-copy: mkdir {s} failed: {s}", .{ dir, @errorName(e) });
        return false;
    };
    // Up-to-date already? Skip to avoid a needless write + mtime bump.
    if (infra_fs.cwd().readFileAlloc(arena, dest_path, MAX_SOURCE_BYTES)) |existing| {
        if (std.mem.eql(u8, existing, content)) return true;
    } else |_| {}

    const tmp_path = std.fmt.allocPrint(arena, "{s}.tmp", .{dest_path}) catch return false;
    // Clear any stale tmp from a prior crashed copy. Missing is the normal case
    // (don't log it); anything else is worth a line but not worth aborting.
    infra_fs.cwd().deleteFile(tmp_path) catch |e| switch (e) {
        error.FileNotFound => {},
        else => log.warn("source-copy: clear tmp {s} failed: {s}", .{ tmp_path, @errorName(e) }),
    };
    var f = infra_fs.cwd().createFile(tmp_path, .{ .truncate = true }) catch |e| {
        log.warn("source-copy: create {s} failed: {s}", .{ tmp_path, @errorName(e) });
        return false;
    };
    f.writeAll(content) catch |e| {
        f.close();
        log.warn("source-copy: write {s} failed: {s}", .{ tmp_path, @errorName(e) });
        return false;
    };
    // Read-only is best-effort: some NAS mounts ignore chmod, but the copy is
    // still worth keeping, so don't fail on it.
    f.chmod(READONLY_MODE) catch |e| log.warn("source-copy: chmod {s} failed: {s}", .{ tmp_path, @errorName(e) });
    f.close();
    infra_fs.cwd().rename(tmp_path, dest_path) catch |e| {
        log.warn("source-copy: rename {s} failed: {s}", .{ dest_path, @errorName(e) });
        return false;
    };
    return true;
}

/// A deferred `add` op. The diff walk buffers these instead of emitting
/// inline so `emitStagedAdds` can group them by section and assign each a
/// staging position before writing the ops.
const PendingAdd = struct {
    inst: export_kicad.FlatInstance,
    fp_name: []const u8,
    canopy_net: ?[]const u8,
    section: []const u8,
};

/// A part's fixed seat in the staging grid: `gi` is its section's cell index
/// (sorted-section order, wrapped at STAGE_SECTIONS_PER_ROW), `slot` is the
/// part's index inside that section's box (sorted-ref order).
const StagingSlot = struct { gi: usize, slot: usize };

/// Diff-independent staging plan built from the *whole* design roster, so a
/// part's staging coordinate is a function of its identity, not of which other
/// parts happen to share the current push. Without this, each push sized and
/// flow-packed boxes from only the parts being added that run, so a smaller
/// follow-up push drew differently-sized boxes from the same origin — landing
/// on top of the still-unplaced boxes from the previous push. `max_n` (the
/// largest section's part count across the design) drives a uniform box size.
const StagingLayout = struct {
    by_ref: std.StringHashMap(StagingSlot),
    max_n: usize,
};

/// Assign every design part a fixed staging seat. Groups the full flattened
/// roster by the same section key `emitStagedAdds` uses (`sectionForRef`, with
/// "" → "(unsectioned)"), sorts sections and the refs within each, and records
/// each ref's cell + slot. Pure function of the design — identical across runs
/// and independent of the board diff.
fn buildStagingLayout(
    arena: std.mem.Allocator,
    insts: []const export_kicad.FlatInstance,
    ref_to_section: *std.StringHashMap([]const u8),
) !StagingLayout {
    var buckets = std.StringHashMap(std.ArrayListUnmanaged([]const u8)).init(arena);
    var order: std.ArrayListUnmanaged([]const u8) = .empty;
    for (insts) |inst| {
        const raw = sectionForRef(inst.ref_des, ref_to_section);
        const key = if (raw.len > 0) raw else "(unsectioned)";
        const e = try buckets.getOrPut(key);
        if (!e.found_existing) {
            e.value_ptr.* = .empty;
            try order.append(arena, key);
        }
        try e.value_ptr.append(arena, inst.ref_des);
    }
    std.mem.sort([]const u8, order.items, {}, lessThanStr);

    var by_ref = std.StringHashMap(StagingSlot).init(arena);
    var max_n: usize = 0;
    for (order.items, 0..) |sec, gi| {
        const refs = buckets.get(sec) orelse continue;
        std.mem.sort([]const u8, refs.items, {}, lessThanStr);
        if (refs.items.len > max_n) max_n = refs.items.len;
        for (refs.items, 0..) |ref, slot| {
            try by_ref.put(ref, .{ .gi = gi, .slot = slot });
        }
    }
    return .{ .by_ref = by_ref, .max_n = max_n };
}

const DiffContext = struct {
    by_uuid: *std.StringHashMap(BoardFp),
    by_ref: *std.StringHashMap(BoardFp),
    /// Board fp KiCad-internal uuid → BoardFp. The add path stamps a new
    /// footprint's KiCad `(uuid …)` equal to the design's canopy_uuid, so
    /// when the canopy_uuid *property* later gets stripped (a swap that
    /// didn't reissue it, a pcbnew round-trip, manual edits) the only
    /// surviving link back to the design instance is this field. Consulted
    /// as a last resort in matchInstance to adopt such orphans in place
    /// instead of spawning a duplicate at the origin.
    by_kicad_uuid: *std.StringHashMap(BoardFp),
    /// Migration mode: design instance uuid → BoardFp it should adopt
    /// placement from. Populated by `buildMigrationIndex`; empty when
    /// --migrate is off.
    by_migration: *std.StringHashMap(BoardFp),
    /// Net-signature relink: design instance uuid → BoardFp it should
    /// adopt. Populated by `buildNetSignatureRelinkIndex` for orphans the
    /// (parent_path, value) migration tier couldn't pair — typically
    /// cross-hierarchy moves like top-level `Q1` → `disp/Q3`. Gated on
    /// --migrate alongside the migration index.
    by_netsig: *std.StringHashMap(BoardFp),
    /// Set of board fp kicad_uuids that some design instance will claim
    /// via its canopy_uuid (by_uuid tier). matchInstance's by_ref fallback
    /// refuses to return any fp whose kicad_uuid is in this set, so two
    /// instances can't collide on one fp via different tiers.
    reserved_kicad_uuids: *std.StringHashMap(void),
    pad_net_map: *std.StringHashMap([]const u8),
    /// "ref|pin" → full (pre-collapse) net name, and full net → the hub pins
    /// on it. Together they let a passive's `canopy_net` field name the device
    /// pin each pad routes to (`U8.C3.VDD33USB / GND`). Passives only.
    pad_full_net: *std.StringHashMap([]const u8),
    net_hub_pins: *std.StringHashMap(std.ArrayListUnmanaged(export_kicad.FlatPin)),
    /// ref-des → the design `(section …)` it was declared in, for the
    /// `canopy_section` field and the section-grouped staging layout. Built
    /// from the design block's top-level sections (whose `.instances` already
    /// include nested sub-section + decouple/series children).
    ref_to_section: *std.StringHashMap([]const u8),
    /// Diff-independent staging seats for every design part, so `emitStagedAdds`
    /// places a new part at the same coordinate on every push. See StagingLayout.
    staging_layout: *const StagingLayout,
    /// The design's premade placement-tool layout (ref-des → x/y/rot in mm/deg),
    /// or null when it has none. A part inserted for the FIRST time whose ref the
    /// layout names is placed at that exact pose instead of the staging grid;
    /// parts the layout omits still fall back to staging. Existing/matched
    /// footprints are never moved. See `loadSyncLayout` and `emitStagedAdds`.
    premade_layout: ?std.StringHashMap(pcb_layout.SyncPose),
    /// Newly-added instances, buffered during the diff walk so the staging
    /// pass can lay them out grouped by section (positions + section boxes)
    /// after every match has been resolved. See `emitStagedAdds`.
    pending_adds: *std.ArrayListUnmanaged(PendingAdd),
    /// "ref_des|net" → count of design pads carrying that net on the instance.
    /// `emitPadNetOps` clears a stale board pad only when its net is a count==1
    /// signal the design moved elsewhere, never a multi-pad GND/power net.
    net_pad_count: *std.StringHashMap(u32),
    matched_uuids: *std.StringHashMap(void),
    /// short EDA footprint name ("c-0201") → canonical KiCad name inside
    /// the lib/footprints/<short>.sexp file ("C_0201_0603Metric"). Used to
    /// skip swap_footprint ops when a manually-placed board fp carries the
    /// KiCad-canonical name but resolves to the same physical footprint as
    /// the design's short name. Populated lazily on first lookup.
    canonical_fp_name: *std.StringHashMap([]const u8),
    /// Fingerprint set of state-asserting ops the agent already pushed
    /// in prior syncs. Owned by ParsedSyncPlan; the diff loop consults
    /// it via the `emitOpUnlessApplied` helper to skip re-emitting ops
    /// that already landed (some KiCad IPC writes don't round-trip via
    /// GetItems, which would otherwise cause indefinite re-emission).
    applied_ops: *std.StringHashMap(void),
    spc: *SyncPlanContext,
    summary: *SyncSummary,
    migrate_heuristic: bool,
    /// Re-bake matched parts' geometry via a same-name swap (see ParsedSyncPlan.refresh).
    refresh: bool,
};

/// Walk every board fp that didn't match any design instance and emit the
/// appropriate orphan-handling op: `remove` when --prune is on, otherwise an
/// informational `flag_stale`. The sync deliberately does NOT lock stale
/// footprints — footprint locking is left entirely to the user in KiCad, so a
/// sync never flips the padlock on (or off) a part.
fn emitStaleOps(
    parsed: ParsedSyncPlan,
    matched_uuids: *std.StringHashMap(void),
    w: anytype,
    first_op: *bool,
    summary: *SyncSummary,
) !void {
    for (parsed.board) |bfp| {
        if (bfp.uuid.len == 0) continue;
        if (matched_uuids.contains(bfp.kicad_uuid)) continue;
        const target = opTargetUuid(bfp);
        if (target.len == 0) continue;
        if (parsed.prune_stale) {
            try emitOp(w, first_op, "remove", .{.{ "uuid", target }});
            summary.removed += 1;
            continue;
        }
        try emitOp(w, first_op, "flag_stale", .{ .{ "uuid", target }, .{ "ref", bfp.ref } });
        summary.flagged_stale += 1;
    }
}

/// Emit a state-asserting op only when the agent hasn't already pushed
/// the same (uuid, op, key, value) fingerprint. When suppressed,
/// increments summary.suppressed and returns false so the caller can
/// avoid bumping its local ops-emitted counter (which would otherwise
/// flag the instance as "updated" with no actual op to point at).
fn emitOpUnlessApplied(
    d: *DiffContext,
    w: anytype,
    first: *bool,
    op: []const u8,
    fields: anytype,
    uuid: []const u8,
    key: []const u8,
    value: []const u8,
) !bool {
    const fp = try appliedOpFingerprint(d.spc.arena, uuid, op, key, value);
    if (d.applied_ops.contains(fp)) {
        d.summary.suppressed += 1;
        return false;
    }
    try emitOp(w, first, op, fields);
    return true;
}

/// Treat a board footprint's name as matching the design's short name when
/// it equals either the short name itself or the canonical KiCad name
/// declared inside `lib/footprints/<short>.sexp`. Legacy boards routinely
/// carry the KiCad-canonical name (`C_0201_0603Metric`) because they were
/// laid out from a `kicad-cli`-exported netlist — without this aliasing
/// every such fp would emit a spurious swap_footprint on the first sync.
fn footprintNameMatches(d: *DiffContext, board_name: []const u8, short: []const u8) bool {
    // Strip the `<lib>:` prefix the legacy Go IPC agent attached to every
    // footprint it created (`eda-sync:c-0201`). KiCad's own footprints
    // also carry a library nickname in the same form (`Capacitor_SMD:…`),
    // and the design-side `short` is always library-bare.
    const board_bare = stripLibPrefix(board_name);
    if (std.mem.eql(u8, board_bare, short)) return true;
    const canonical = canonicalFootprintName(d, short) orelse return false;
    return std.mem.eql(u8, board_bare, canonical);
}

/// Resolve the KiCad-canonical name for `short` ("c-0201" → "C_0201_0603Metric"),
/// cached per-request. Returns null when the .sexp file is missing or
/// doesn't parse as a footprint form.
fn canonicalFootprintName(d: *DiffContext, short: []const u8) ?[]const u8 {
    return canonicalFootprintNameImpl(d, short) catch null;
}

fn canonicalFootprintNameImpl(d: *DiffContext, short: []const u8) !?[]const u8 {
    if (d.canonical_fp_name.get(short)) |cached| {
        if (cached.len == 0) return null;
        return cached;
    }
    const fp_path = try std.fmt.allocPrint(d.spc.arena, PATH_FMT_FP_SEXP, .{ d.spc.project_dir, short });
    const fp_source = infra_fs.cwd().readFileAlloc(d.spc.arena, fp_path, MAX_FOOTPRINT_BYTES) catch {
        try d.canonical_fp_name.put(short, "");
        return null;
    };
    const name = netlist_mod.extractFootprintName(d.spc.arena, fp_source) catch {
        try d.canonical_fp_name.put(short, "");
        return null;
    };
    try d.canonical_fp_name.put(short, name);
    return name;
}

/// Extract the parent-path prefix of a hierarchical ref-des. Returns the
/// empty string for a top-level ref. e.g. `"adc1/C146"` → `"adc1/"`,
/// `"C18"` → `""`.
fn parentPathOf(ref: []const u8) []const u8 {
    if (std.mem.lastIndexOfScalar(u8, ref, '/')) |i| return ref[0 .. i + 1];
    return "";
}

/// Build the (parent_path, value) key used for migration-mode heuristic
/// matching. We deliberately omit footprint_name: legacy boards routinely
/// carry KiCad-canonical names (`C_0201_0603Metric`) while the design
/// emits EDA-short names (`c-0201`). The swap_footprint op the diff loop
/// emits afterwards handles the rename, and value is specific enough to
/// keep different cap sizes apart within a sub-section.
fn heuristicKey(arena: std.mem.Allocator, parent: []const u8, _: []const u8, value: []const u8) std.mem.Allocator.Error![]const u8 {
    return std.fmt.allocPrint(arena, "{s}|{s}", .{ parent, value });
}

/// Decide which board fp a design instance pairs with, using the two
/// non-heuristic tiers (canopy uuid, then reference). Refuses any
/// match — including by_uuid — for a fp that another instance has
/// already claimed in this walk, and refuses by_ref matches whose fp
/// is reserved by some other instance's canopy_uuid. Without those
/// guards, legacy boards (where Reference/canopy_uuid disagree on the
/// same physical fp, or two design instances share a canopy_uuid)
/// produce two design instances both writing to one fp and emit
/// contradictory ops every sync. Pure function; extracted from
/// `matchInstance` so the collision rules are directly testable.
fn pickByUuidOrRef(
    inst_uuid: []const u8,
    inst_ref: []const u8,
    by_uuid: *std.StringHashMap(BoardFp),
    by_ref: *std.StringHashMap(BoardFp),
    reserved_kicad_uuids: *std.StringHashMap(void),
    already_claimed: *std.StringHashMap(void),
) ?BoardFp {
    if (by_uuid.get(inst_uuid)) |m| {
        if (!already_claimed.contains(m.kicad_uuid)) return m;
    }
    if (inst_ref.len > 0) {
        if (by_ref.get(inst_ref)) |m| {
            if (!reserved_kicad_uuids.contains(m.kicad_uuid) and
                !already_claimed.contains(m.kicad_uuid))
            {
                return m;
            }
        }
    }
    return null;
}

/// Adopt-in-place fallback: a board fp whose KiCad-internal uuid equals the
/// design instance's canopy_uuid. That equality only arises for footprints
/// the add path created (it stamps KiCad uuid := canopy_uuid), so this link
/// survives even after the canopy_uuid *property* is stripped (a swap that
/// didn't reissue it, a pcbnew round-trip). KiCad uuids are random and canopy
/// uuids are SHA-derived, so a coincidental cross-match is impossible — this
/// strictly recovers our own orphans. Refuses an fp another instance already
/// claimed in this walk. Pure; extracted from `matchInstance` to be testable.
fn pickByKicadUuid(
    inst_uuid: []const u8,
    by_kicad_uuid: *std.StringHashMap(BoardFp),
    already_claimed: *std.StringHashMap(void),
) ?BoardFp {
    if (by_kicad_uuid.get(inst_uuid)) |m| {
        if (!already_claimed.contains(m.kicad_uuid)) return m;
    }
    return null;
}

fn matchInstance(d: *DiffContext, inst: export_kicad.FlatInstance) ?BoardFp {
    // Net-signature relink wins over canopy uuid: the relink builder
    // only populates a pairing when there's an orphan board fp with the
    // same wiring + footprint AND no other claimant. When that fires
    // for a design instance whose canopy uuid happens to also point at
    // a (likely agent-created duplicate at origin) by_uuid entry, the
    // netsig pair wins and the duplicate falls into stale — exactly
    // what the user wants when running --migrate to recover a board
    // where a hierarchy move (top-level `Q1` → `disp/Q3`) caused the
    // sync to create a clone instead of relinking the existing fp.
    if (d.migrate_heuristic) {
        if (d.by_netsig.get(inst.uuid)) |m| return m;
    }
    if (pickByUuidOrRef(inst.uuid, inst.ref_des, d.by_uuid, d.by_ref, d.reserved_kicad_uuids, d.matched_uuids)) |m| return m;
    // Adopt-in-place: a board fp whose KiCad-internal uuid equals this
    // instance's canopy_uuid — a self-stamped orphan whose canopy_uuid
    // property was stripped (a swap that didn't reissue it, a pcbnew
    // round-trip). Preserves the user's placement instead of adding a
    // duplicate at the origin. handleMatched re-stamps canopy_uuid for
    // every match tier (Option G), so this tier emits nothing itself.
    if (pickByKicadUuid(inst.uuid, d.by_kicad_uuid, d.matched_uuids)) |m| return m;
    // Migration tier (parent_path/value). canopy_uuid stamping + ref-des
    // rename are emitted by handleMatched for every match tier (Option G),
    // so matchInstance no longer emits anything itself.
    return heuristicMatch(d, inst);
}

/// Migration-mode lookup for (parent_path, value) pairings. Gated on
/// --migrate. Net-signature relink is checked separately in
/// `matchInstance` (it must beat by_uuid to recover from agent-created
/// duplicates) so it lives outside this helper.
fn heuristicMatch(d: *DiffContext, inst: export_kicad.FlatInstance) ?BoardFp {
    if (!d.migrate_heuristic) return null;
    return d.by_migration.get(inst.uuid);
}

/// Build a sortable signature string of a design instance's pad-net
/// assignments, derived from the diff loop's `pad_net_map`. Format is
/// `<pad1>=<net1>;<pad2>=<net2>;…` with pads sorted lexicographically so
/// two instances with identical wiring produce identical strings.
/// Returns "" when the instance has no pads on named nets (NC-only),
/// signalling the caller to skip it for relinking.
fn designInstanceNetSig(
    arena: std.mem.Allocator,
    inst_ref_des: []const u8,
    pad_net_map: *std.StringHashMap([]const u8),
) ![]const u8 {
    var pairs: std.ArrayListUnmanaged(struct { pad: []const u8, net: []const u8 }) = .empty;
    var it = pad_net_map.iterator();
    while (it.next()) |entry| {
        const key = entry.key_ptr.*;
        const sep = std.mem.indexOfScalar(u8, key, '|') orelse continue;
        if (!std.mem.eql(u8, key[0..sep], inst_ref_des)) continue;
        try pairs.append(arena, .{ .pad = key[sep + 1 ..], .net = entry.value_ptr.* });
    }
    if (pairs.items.len == 0) return "";
    std.mem.sort(@TypeOf(pairs.items[0]), pairs.items, {}, struct {
        fn lessThan(_: void, a: @TypeOf(pairs.items[0]), b: @TypeOf(pairs.items[0])) bool {
            return std.mem.lessThan(u8, a.pad, b.pad);
        }
    }.lessThan);
    var buf: std.ArrayListUnmanaged(u8) = .empty;
    const w = buf.writer(arena);
    for (pairs.items) |p| try w.print("{s}={s};", .{ p.pad, p.net });
    return buf.items;
}

/// Same signature format as `designInstanceNetSig`, computed from the
/// pad list the agent reported for a board fp. Empty when the fp has no
/// pads on named nets.
fn boardFpNetSig(arena: std.mem.Allocator, bfp: BoardFp) ![]const u8 {
    var pairs: std.ArrayListUnmanaged(struct { pad: []const u8, net: []const u8 }) = .empty;
    for (bfp.pads) |p| {
        if (p.net.len == 0) continue;
        try pairs.append(arena, .{ .pad = p.number, .net = p.net });
    }
    if (pairs.items.len == 0) return "";
    std.mem.sort(@TypeOf(pairs.items[0]), pairs.items, {}, struct {
        fn lessThan(_: void, a: @TypeOf(pairs.items[0]), b: @TypeOf(pairs.items[0])) bool {
            return std.mem.lessThan(u8, a.pad, b.pad);
        }
    }.lessThan);
    var buf: std.ArrayListUnmanaged(u8) = .empty;
    const w = buf.writer(arena);
    for (pairs.items) |p| try w.print("{s}={s};", .{ p.pad, p.net });
    return buf.items;
}

/// Pair orphan board fps with orphan design instances by exact net
/// signature. "Orphan" = won't pair via canopy_uuid match, ref-des
/// match, or the (parent_path, value) migration tier. Only emits a pair
/// when the signature is unique on BOTH sides — if two board fps have
/// the same signature, or two design instances do, we skip both rather
/// than guess. The footprint name must also match (canonical or short).
///
/// Output keyed by design instance uuid so heuristicMatch resolves in O(1).
fn buildNetSignatureRelinkIndex(
    arena: std.mem.Allocator,
    project_dir: []const u8,
    board: []const BoardFp,
    instances: []const export_kicad.FlatInstance,
    by_uuid: *std.StringHashMap(BoardFp),
    _: *std.StringHashMap(BoardFp), // by_ref — unused since the netsig tier
    // deliberately considers insts that already match by_ref so it can
    // pair them with an orphan instead of an agent-created duplicate.
    by_migration: *std.StringHashMap(BoardFp),
    pad_net_map: *std.StringHashMap([]const u8),
    out: *std.StringHashMap(BoardFp),
) !void {
    // The migration index is keyed by design uuid → BoardFp; mark which
    // board fps it already claimed so we don't double-pair them.
    var migration_claimed_kids = std.StringHashMap(void).init(arena);
    var mit = by_migration.iterator();
    while (mit.next()) |entry| {
        const m = entry.value_ptr.*;
        if (m.kicad_uuid.len > 0) try migration_claimed_kids.put(m.kicad_uuid, {});
    }

    // Bucket orphan board fps by netsig (with footprint name appended so
    // a stray `c-0201`-vs-`r-0201` collision on pads can't pair).
    var board_by_sig = std.StringHashMap(std.ArrayListUnmanaged(BoardFp)).init(arena);
    for (board) |bfp| {
        if (bfp.uuid.len == 0) continue;
        if (migration_claimed_kids.contains(bfp.kicad_uuid)) continue;
        // Skip board fps whose canopy uuid already pairs with a design
        // instance directly — those are reachable via by_uuid and not
        // orphans at all.
        if (by_uuid.contains(bfp.uuid) and matchesUuidExactlyInDesign(bfp.uuid, instances)) continue;
        const sig = try boardFpNetSig(arena, bfp);
        if (sig.len == 0) continue;
        const key = try std.fmt.allocPrint(arena, "{s}|{s}", .{ bfp.footprint_name, sig });
        const e = try board_by_sig.getOrPut(key);
        if (!e.found_existing) e.value_ptr.* = .empty;
        try e.value_ptr.append(arena, bfp);
    }

    // Bucket design instances by netsig. We don't skip insts that match
    // by_uuid or by_ref — those matches may be to an agent-created
    // duplicate at the origin (same canopy_uuid AND same ref-des as the
    // design instance because the previous sync created it from the
    // design state, but at the wrong physical location). Letting netsig
    // consider this instance lets it pair with the genuine orphan
    // board fp instead, dropping the duplicate into stale.
    //
    // Migration-tier matches are already a deterministic pairing so we
    // skip those — netsig overriding them would just churn.
    var design_by_sig = std.StringHashMap(std.ArrayListUnmanaged(export_kicad.FlatInstance)).init(arena);
    // We don't skip ANY insts (not by_uuid, by_ref, or by_migration) so
    // netsig can override every other tier when it finds a unique
    // matching orphan. The pair only fires when the signature is unique
    // on both sides — if migration's pair is a legit physical match
    // they'd share the signature with the orphan and the resulting
    // ambiguity (2 board fps, 1 design inst) skips the pair.
    for (instances) |inst| {
        const sig = try designInstanceNetSig(arena, inst.ref_des, pad_net_map);
        if (sig.len == 0) continue;
        const fp_short = stripLibPrefix(inst.footprint);
        const key = try std.fmt.allocPrint(arena, "{s}|{s}", .{ fp_short, sig });
        const e = try design_by_sig.getOrPut(key);
        if (!e.found_existing) e.value_ptr.* = .empty;
        try e.value_ptr.append(arena, inst);
    }

    // For each design signature with exactly one orphan instance AND
    // exactly one orphan board fp on the same signature, pair them.
    // Footprint-name in the key uses the EDA short name for the design
    // side and the KiCad-canonical name for the board side. We compare
    // by trying both forms when looking up — if the board's canonical
    // form maps back to the same short name, the signatures match.
    var dit = design_by_sig.iterator();
    while (dit.next()) |entry| {
        if (entry.value_ptr.items.len != 1) continue;
        const inst = entry.value_ptr.items[0];
        const sig_after_pipe = std.mem.indexOfScalar(u8, entry.key_ptr.*, '|') orelse continue;
        const sig = entry.key_ptr.*[sig_after_pipe + 1 ..];

        // Try matching the board side first by EDA short name (same key),
        // then by walking all board buckets whose sig matches and whose
        // footprint name canonicalises to the same short form.
        var found_board: ?BoardFp = null;
        var ambiguous = false;
        if (board_by_sig.get(entry.key_ptr.*)) |bucket| {
            if (bucket.items.len == 1) found_board = bucket.items[0];
            if (bucket.items.len > 1) ambiguous = true;
        }
        if (found_board == null and !ambiguous) {
            // Fall back: scan all board buckets, accepting any whose
            // signature suffix matches and whose footprint name is the
            // KiCad-canonical alias of the design's short name.
            const fp_short = stripLibPrefix(inst.footprint);
            var bit = board_by_sig.iterator();
            while (bit.next()) |bentry| {
                const bkey = bentry.key_ptr.*;
                const bsep = std.mem.indexOfScalar(u8, bkey, '|') orelse continue;
                if (!std.mem.eql(u8, bkey[bsep + 1 ..], sig)) continue;
                const board_fp_name = bkey[0..bsep];
                if (!std.mem.eql(u8, board_fp_name, fp_short) and
                    !boardFpNameAliasesShort(arena, project_dir, board_fp_name, fp_short))
                    continue;
                if (bentry.value_ptr.items.len != 1) {
                    ambiguous = true;
                    break;
                }
                if (found_board != null) {
                    ambiguous = true;
                    break;
                }
                found_board = bentry.value_ptr.items[0];
            }
        }
        if (ambiguous) continue;
        const board_fp = found_board orelse continue;
        try out.put(inst.uuid, board_fp);
    }
}

/// Return true when at least one design instance carries `uuid` as its
/// canopy uuid — used by net-signature relink to skip board fps that
/// already pair through the by_uuid index.
fn matchesUuidExactlyInDesign(uuid: []const u8, instances: []const export_kicad.FlatInstance) bool {
    for (instances) |inst| {
        if (std.mem.eql(u8, inst.uuid, uuid)) return true;
    }
    return false;
}

/// True when `board_name` is the KiCad-canonical alias of EDA short name
/// `short` (e.g. `C_0201_0603Metric` ↔ `c-0201`). Reads
/// `lib/footprints/<short>.sexp` on demand; failures degrade to false so
/// a missing library doesn't pair the wrong fp.
fn boardFpNameAliasesShort(arena: std.mem.Allocator, project_dir: []const u8, board_name: []const u8, short: []const u8) bool {
    if (std.mem.eql(u8, board_name, short)) return true;
    // Re-read the .sexp on demand — this is called during relink-index
    // construction (once per sync), not per-instance in the diff loop,
    // so the cost is fine.
    const fp_path = std.fmt.allocPrint(arena, PATH_FMT_FP_SEXP, .{ project_dir, short }) catch return false;
    const fp_source = infra_fs.cwd().readFileAlloc(arena, fp_path, MAX_FOOTPRINT_BYTES) catch return false;
    const canonical = netlist_mod.extractFootprintName(arena, fp_source) catch return false;
    return std.mem.eql(u8, board_name, canonical);
}

/// Pair board footprints with design instances by (parent_path,
/// footprint_name, value). When a key has the same count N on both sides,
/// pair them deterministically — sort each side by ref-des and match
/// element-i to element-i. The N==M skip rule prevents a 12-cap section
/// from silently absorbing 8 caps and dropping 4 on the floor.
///
/// The pairings populate `out` keyed by design instance uuid so the diff
/// loop can resolve each `inst.uuid` → adopted `BoardFp` in O(1).
fn buildMigrationIndex(
    arena: std.mem.Allocator,
    board: []const BoardFp,
    instances: []const export_kicad.FlatInstance,
    out: *std.StringHashMap(BoardFp),
) !void {
    // Index design + board by heuristic key. Hash → list of refs; we sort
    // the lists later for deterministic pairing.
    var board_groups = std.StringHashMap(std.ArrayListUnmanaged(BoardFp)).init(arena);
    var design_groups = std.StringHashMap(std.ArrayListUnmanaged(export_kicad.FlatInstance)).init(arena);

    for (board) |bfp| {
        const key = try heuristicKey(arena, parentPathOf(bfp.ref), bfp.footprint_name, bfp.value);
        const e = try board_groups.getOrPut(key);
        if (!e.found_existing) e.value_ptr.* = .empty;
        try e.value_ptr.append(arena, bfp);
    }
    for (instances) |inst| {
        const fp_name = stripLibPrefix(inst.footprint);
        const key = try heuristicKey(arena, parentPathOf(inst.ref_des), fp_name, inst.value);
        const e = try design_groups.getOrPut(key);
        if (!e.found_existing) e.value_ptr.* = .empty;
        try e.value_ptr.append(arena, inst);
    }

    var it = design_groups.iterator();
    while (it.next()) |entry| {
        const dgroup = entry.value_ptr.*;
        const bgroup = board_groups.get(entry.key_ptr.*) orelse continue;
        if (dgroup.items.len != bgroup.items.len) continue;
        // Sort both sides by ref-des so the pairing is stable across
        // re-runs and across rebuilds. Different runs of --migrate against
        // the same inputs MUST produce the same plan.
        std.mem.sort(BoardFp, bgroup.items, {}, lessByRef);
        std.mem.sort(export_kicad.FlatInstance, dgroup.items, {}, lessByDesignRef);
        for (dgroup.items, bgroup.items) |inst, bfp| {
            try out.put(inst.uuid, bfp);
        }
    }
}

fn lessByRef(_: void, a: BoardFp, b: BoardFp) bool {
    return std.mem.lessThan(u8, a.ref, b.ref);
}

fn lessByDesignRef(_: void, a: export_kicad.FlatInstance, b: export_kicad.FlatInstance) bool {
    return std.mem.lessThan(u8, a.ref_des, b.ref_des);
}

fn handleMatched(
    d: *DiffContext,
    inst: export_kicad.FlatInstance,
    m: BoardFp,
    fp_name_short: []const u8,
    canopy_net: ?[]const u8,
    section: []const u8,
    w: anytype,
    first: *bool,
    ops_emitted: *u32,
) !void {
    // Track matches by KiCad-internal UUID — that field is always
    // populated, whereas canopy_uuid is empty for ref-des-fallback
    // matches until the backfill op lands on a future sync.
    if (m.kicad_uuid.len > 0) try d.matched_uuids.put(m.kicad_uuid, {});
    const target = opTargetUuid(m);
    if (!footprintNameMatches(d, m.footprint_name, fp_name_short)) {
        if (loadKicadMod(d.spc, fp_name_short, inst.component)) |kmod| {
            const fp_def = loadFootprintDef(d.spc, fp_name_short);
            try emitSwapOp(w, first, target, fp_name_short, kmod, fp_def, inst.ref_des, d.pad_net_map);
            d.summary.swapped += 1;
            ops_emitted.* += 1;
        }
    } else if (d.refresh) {
        // Force-refresh: the footprint name already matches, but `?refresh=1`
        // re-bakes the geometry so a board built before the silk/fab fixes
        // picks up the outlines. swapFootprint preserves at/uuid/layer/locked
        // + custom properties and re-applies pad nets, so nothing moves.
        if (loadKicadMod(d.spc, fp_name_short, inst.component)) |kmod| {
            const fp_def = loadFootprintDef(d.spc, fp_name_short);
            try emitSwapOp(w, first, target, fp_name_short, kmod, fp_def, inst.ref_des, d.pad_net_map);
            d.summary.swapped += 1;
            ops_emitted.* += 1;
        }
    }
    if (!std.mem.eql(u8, m.ref, inst.ref_des)) {
        if (try emitOpUnlessApplied(d, w, first, OP_SET_FIELD, .{
            .{ "uuid", target },
            .{ "field", "reference" },
            .{ "value", inst.ref_des },
        }, target, "reference", inst.ref_des)) {
            ops_emitted.* += 1;
        }
    }
    if (!std.mem.eql(u8, m.value, inst.value)) {
        if (try emitOpUnlessApplied(d, w, first, OP_SET_FIELD, .{
            .{ "uuid", target },
            .{ "field", "value" },
            .{ "value", inst.value },
        }, target, "value", inst.value)) {
            ops_emitted.* += 1;
        }
    }
    // Stamp/realign canopy_uuid whenever it differs from the design's id —
    // this covers BOTH the eager case (a matched fp that has no canopy_uuid
    // yet: by_ref backfill, manual placement, or a migration-tier match) and
    // the drift case (legacy long-form, --migrate pairing). opTargetUuid falls
    // back to kicad_uuid so the agent resolves the fp before canopy_uuid is
    // set. After this op lands, the next sync UUID-matches directly. This is
    // the single stamping point for every match tier (Option G).
    if (!std.mem.eql(u8, m.uuid, inst.uuid)) {
        if (try emitOpUnlessApplied(d, w, first, OP_SET_FIELD, .{
            .{ "uuid", target },
            .{ "field", FIELD_CANOPY_UUID },
            .{ "value", inst.uuid },
        }, target, FIELD_CANOPY_UUID, inst.uuid)) {
            ops_emitted.* += 1;
        }
    }
    // Push every design property to KiCad as a custom Field, so adding a
    // new BOM column (manufacturer, datasheet, supplier_pn, …) is a pure
    // server-side change with no agent update. Skip property keys that
    // collide with KiCad's built-in ref/value/footprint handling — those
    // travel through dedicated set_field ops above. The canonical-name map
    // upper-cases well-known KiCad field names (mpn → MPN) so manually-
    // placed footprints with the standard column name match cleanly.
    for (inst.properties) |p| {
        if (skipDesignProperty(p.key)) continue;
        if (p.value.len == 0) continue;
        const field_name = canonicalFieldName(p.key);
        const board_value = m.fields.get(field_name) orelse "";
        if (std.mem.eql(u8, board_value, p.value)) continue;
        if (try emitOpUnlessApplied(d, w, first, OP_SET_FIELD, .{
            .{ "uuid", target },
            .{ "field", field_name },
            .{ "value", p.value },
        }, target, field_name, p.value)) {
            ops_emitted.* += 1;
        }
    }
    // Passive routing hint: each pad's intended (device.pin.net) destination,
    // so a layout engineer reading a bare cap/resistor sees what it bridges.
    if (canopy_net) |cn| {
        const board_value = m.fields.get(FIELD_CANOPY_NET) orelse "";
        if (!std.mem.eql(u8, board_value, cn)) {
            if (try emitOpUnlessApplied(d, w, first, OP_SET_FIELD, .{
                .{ "uuid", target },
                .{ "field", FIELD_CANOPY_NET },
                .{ "value", cn },
            }, target, FIELD_CANOPY_NET, cn)) {
                ops_emitted.* += 1;
            }
        }
    }
    // Section provenance: which design `(section …)` this part came from.
    if (section.len > 0) {
        const board_value = m.fields.get(FIELD_CANOPY_SECTION) orelse "";
        if (!std.mem.eql(u8, board_value, section)) {
            if (try emitOpUnlessApplied(d, w, first, OP_SET_FIELD, .{
                .{ "uuid", target },
                .{ "field", FIELD_CANOPY_SECTION },
                .{ "value", section },
            }, target, FIELD_CANOPY_SECTION, section)) {
                ops_emitted.* += 1;
            }
        }
    }
    ops_emitted.* += try emitPadNetOps(d, w, first, target, inst.ref_des, d.pad_net_map, m.pads);
}

/// Property keys we deliberately don't push to KiCad as custom fields:
/// `value`/`footprint` are KiCad-built-in (already handled via dedicated
/// set_field ops on inst.value / inst.footprint), and `description` would
/// collide with the description KiCad reads from the footprint library.
fn skipDesignProperty(key: []const u8) bool {
    if (std.mem.eql(u8, key, "value")) return true;
    if (std.mem.eql(u8, key, "footprint")) return true;
    if (std.mem.eql(u8, key, "description")) return true;
    return false;
}

/// Canonicalise design property keys to KiCad's standard field-column
/// names so users with manually-placed footprints (already carrying
/// "MPN", "Manufacturer", …) don't see a duplicate lower-cased twin land
/// on first sync. Unknown keys pass through unchanged so user-defined
/// property keys still work.
fn canonicalFieldName(key: []const u8) []const u8 {
    if (std.mem.eql(u8, key, "mpn")) return "MPN";
    if (std.mem.eql(u8, key, "manufacturer")) return "Manufacturer";
    if (std.mem.eql(u8, key, "datasheet")) return "Datasheet";
    return key;
}

/// Strip a "subblock/" path prefix from a ref-des — "ldo/U1" → "U1".
fn shortRefLocal(ref: []const u8) []const u8 {
    if (std.mem.lastIndexOfScalar(u8, ref, '/')) |i| return ref[i + 1 ..];
    return ref;
}

/// A passive spoke — R/C/L/F/D prefix. Mirrors render_svg `isHub`: only
/// these carry a meaningful `canopy_net` field (it names the hub pin the
/// spoke bridges to); everything else is a hub and is skipped.
fn isPassiveRef(ref: []const u8) bool {
    const s = shortRefLocal(ref);
    if (s.len == 0) return false;
    return switch (s[0]) {
        'R', 'C', 'L', 'F', 'D' => true,
        else => false,
    };
}

/// A hub — anything that isn't a passive spoke (U/J/P/X/Q ICs/connectors
/// plus any unknown prefix). These are the endpoints a passive's
/// `canopy_net` field points at.
fn isHubRef(ref: []const u8) bool {
    return !isPassiveRef(ref);
}

/// One pad of a passive, with both the display net (collapsed, dedup'd —
/// what set_pad_net assigns) and the full pre-collapse net (used to find
/// the hub pin the pad routes to).
const CanopyPad = struct { pin: []const u8, display: []const u8, full: []const u8 };

fn lessByPadNum(_: void, a: CanopyPad, b: CanopyPad) bool {
    const an = std.fmt.parseInt(u32, a.pin, 10) catch null;
    const bn = std.fmt.parseInt(u32, b.pin, 10) catch null;
    if (an != null and bn != null) return an.? < bn.?;
    return std.mem.lessThan(u8, a.pin, b.pin);
}

/// Build the `canopy_net` field value for one passive: a ` / `-joined,
/// pad-ordered list where each pad reads `<destRef>.<destPin>.<net>` when
/// it sits on exactly one hub pin (e.g. `U8.C3.VDD33USB`), or just `<net>`
/// when the net has zero or many hub pins (e.g. a `GND` rail). Returns null
/// when the passive has no connected pads.
fn buildCanopyNetValue(
    arena: std.mem.Allocator,
    ref_des: []const u8,
    pad_net_map: *std.StringHashMap([]const u8),
    pad_full_net: *std.StringHashMap([]const u8),
    net_hub_pins: *std.StringHashMap(std.ArrayListUnmanaged(export_kicad.FlatPin)),
) !?[]const u8 {
    var pads: std.ArrayListUnmanaged(CanopyPad) = .empty;
    var it = pad_net_map.iterator();
    while (it.next()) |entry| {
        const k = entry.key_ptr.*;
        const sep = std.mem.indexOfScalar(u8, k, '|') orelse continue;
        if (!std.mem.eql(u8, k[0..sep], ref_des)) continue;
        const full = pad_full_net.get(k) orelse entry.value_ptr.*;
        try pads.append(arena, .{ .pin = k[sep + 1 ..], .display = entry.value_ptr.*, .full = full });
    }
    if (pads.items.len == 0) return null;
    std.mem.sort(CanopyPad, pads.items, {}, lessByPadNum);

    var buf: std.ArrayListUnmanaged(u8) = .empty;
    const w = buf.writer(arena);
    for (pads.items, 0..) |p, i| {
        if (i > 0) try w.writeAll(" / ");
        // Count the hub pins on this pad's full net (excluding the passive
        // itself). Exactly one → name that device pin; otherwise the net
        // alone, since a rail with many pins has no single target.
        var ep: ?export_kicad.FlatPin = null;
        var count: u32 = 0;
        if (net_hub_pins.get(p.full)) |hubs| {
            for (hubs.items) |hp| {
                if (std.mem.eql(u8, hp.ref_des, ref_des)) continue;
                count += 1;
                if (ep == null) ep = hp;
            }
        }
        if (count == 1) {
            try w.print("{s}.{s}.{s}", .{ shortRefLocal(ep.?.ref_des), ep.?.pin, p.display });
        } else {
            try w.writeAll(p.display);
        }
    }
    return buf.items;
}

fn handleInstance(d: *DiffContext, inst: export_kicad.FlatInstance, w: anytype, first: *bool) !void {
    const fp_name_short = stripLibPrefix(inst.footprint);
    // Only passives carry a canopy_net field (it names the hub pin each pad
    // bridges to). Computed once here so both the matched set_field path and
    // the add bake-in path use the same value.
    const canopy_net = if (isPassiveRef(inst.ref_des))
        try buildCanopyNetValue(d.spc.arena, inst.ref_des, d.pad_net_map, d.pad_full_net, d.net_hub_pins)
    else
        null;
    const section = sectionForRef(inst.ref_des, d.ref_to_section);
    var ops_emitted: u32 = 0;
    if (matchInstance(d, inst)) |m| {
        try handleMatched(d, inst, m, fp_name_short, canopy_net, section, w, first, &ops_emitted);
        // Only count as "updated" when at least one op was actually
        // emitted — otherwise every no-diff sync would still surface
        // "Updated: N" to the user even though nothing changed.
        if (ops_emitted > 0) d.summary.updated += 1;
        return;
    }
    // Defer the add — `emitStagedAdds` lays buffered adds out grouped by
    // section once the whole walk is done. fp_name / canopy_net / section
    // are resolved now; kmod + footprint_def are loaded at emit time.
    try d.pending_adds.append(d.spc.arena, .{
        .inst = inst,
        .fp_name = fp_name_short,
        .canopy_net = canopy_net,
        .section = section,
    });
}

/// Resolve which design section an instance belongs to. Sub-block parts
/// (hierarchical ref-des like "usb/U1") are attributed to their sub-block
/// name; top-level parts use the `(section …)` they were declared in;
/// anything else returns "" (no box, no canopy_section field).
fn sectionForRef(ref_des: []const u8, ref_to_section: *std.StringHashMap([]const u8)) []const u8 {
    if (std.mem.indexOfScalar(u8, ref_des, '/')) |i| return ref_des[0..i];
    return ref_to_section.get(ref_des) orelse "";
}

fn emitOp(w: anytype, first: *bool, op: []const u8, fields: anytype) !void {
    if (!first.*) try w.*.writeAll(",");
    first.* = false;
    try w.*.writeAll("{\"op\":\"");
    try w.*.writeAll(op);
    try w.*.writeAll("\"");
    inline for (fields) |kv| {
        try w.*.writeAll(",");
        try w.*.print("\"{s}\":", .{kv[0]});
        try json_writer.writeString(w.*, kv[1]);
    }
    try w.*.writeAll("}");
}

fn emitAddOp(
    w: anytype,
    first: *bool,
    inst: export_kicad.FlatInstance,
    fp_name: []const u8,
    kmod: []const u8,
    fp_def_json: ?[]const u8,
    pad_net_map: *std.StringHashMap([]const u8),
    x_nm: i64,
    y_nm: i64,
    rot_deg: f64,
    canopy_net: ?[]const u8,
    section: []const u8,
) !void {
    if (!first.*) try w.*.writeAll(",");
    first.* = false;
    try w.*.writeAll("{\"op\":\"add\",\"uuid\":");
    try json_writer.writeString(w.*, inst.uuid);
    try w.*.writeAll(",\"ref\":");
    try json_writer.writeString(w.*, inst.ref_des);
    try w.*.writeAll(",\"value\":");
    try json_writer.writeString(w.*, inst.value);
    try w.*.writeAll(",\"footprint_name\":");
    try json_writer.writeString(w.*, fp_name);
    try w.*.writeAll(",\"kicad_mod\":");
    try json_writer.writeString(w.*, kmod);
    if (fp_def_json) |def| {
        try w.*.writeAll(",\"footprint_def\":");
        try w.*.writeAll(def);
    }
    // canopy_net / canopy_section as top-level op fields too: the IPC agent
    // reads them from the baked footprint_def above, but the file-based
    // writer (the webpage button) bakes them onto the fp from these fields
    // so they land on the first sync rather than waiting for a follow-up.
    if (canopy_net) |cn| {
        if (cn.len > 0) {
            try w.*.writeAll(",\"canopy_net\":");
            try json_writer.writeString(w.*, cn);
        }
    }
    if (section.len > 0) {
        try w.*.writeAll(",\"canopy_section\":");
        try json_writer.writeString(w.*, section);
    }
    // Design properties (MPN, Manufacturer, Datasheet, … — every BOM column
    // except KiCad built-ins) baked into the add so they land on the FIRST
    // sync. The IPC agent reads these from footprint_def above; the file-based
    // writer reads this array. Without it the file path created the fp with
    // only ref/value/canopy_*, and the rest didn't appear until a 2nd sync
    // emitted set_field ops against the now-existing part ("press sync twice").
    // Filtering mirrors the update path (skipDesignProperty + canonicalFieldName)
    // so the first sync bakes exactly what the second would set — no residual diff.
    try w.*.writeAll(",\"properties\":[");
    var prop_first = true;
    for (inst.properties) |p| {
        if (skipDesignProperty(p.key)) continue;
        if (p.value.len == 0) continue;
        if (!prop_first) try w.*.writeAll(",");
        prop_first = false;
        try w.*.writeAll("{\"key\":");
        try json_writer.writeString(w.*, canonicalFieldName(p.key));
        try w.*.writeAll(",\"value\":");
        try json_writer.writeString(w.*, p.value);
        try w.*.writeAll("}");
    }
    try w.*.writeAll("]");
    // Staging position (board nm). Both paths move the new fp here; (0,0)
    // means "no staging hint" (the part stays at the origin).
    try w.*.print(",\"x\":{d},\"y\":{d}", .{ x_nm, y_nm });
    // Orientation (deg, CCW). Only emitted for a premade-layout placement; the
    // staging grid passes 0, leaving the writer's bare `(at x y)` form.
    if (rot_deg != 0) try w.*.print(",\"rot\":{d}", .{rot_deg});
    try w.*.writeAll(",\"pad_nets\":");
    try writePadNetsArray(w.*, inst.ref_des, pad_net_map);
    try w.*.writeAll("}");
}

fn lessThanStr(_: void, a: []const u8, b: []const u8) bool {
    return std.mem.lessThan(u8, a, b);
}

/// Number of part columns for a roughly-square box holding `n` parts.
fn boxCols(n: usize) usize {
    if (n <= 1) return 1;
    const c: usize = @intFromFloat(@ceil(@sqrt(@as(f64, @floatFromInt(n)))));
    return if (c < 1) 1 else c;
}

/// Build a BoardGraphicShape rectangle (unfilled outline) on Dwgs.User as
/// an Any-wrapped proto-canonical object string. Mirrors `writeBoardShapeOpen`
/// + geometry but standalone rather than an inline footprint child.
fn boardRectItem(arena: std.mem.Allocator, x1: f64, y1: f64, x2: f64, y2: f64) ![]const u8 {
    var buf: std.ArrayListUnmanaged(u8) = .empty;
    const w = buf.writer(arena);
    try w.writeAll(PROTO_ANY_OPEN ++ PROTO_TYPE_URL_BOARDSHAPE ++ "\",\"layer\":\"" ++ PROTO_LAYER_DWGS_USER ++ "\",");
    try w.writeAll("\"shape\":{\"attributes\":{\"stroke\":{\"width\":");
    try w.print("{{\"valueNm\":{d}}},\"style\":\"SLS_DEFAULT\"}},", .{mmToNm(STAGE_BOX_STROKE_MM)});
    try w.writeAll("\"fill\":{\"fillType\":\"GFT_UNFILLED\"}},");
    try writeRectGeom(w, x1, y1, x2, y2);
    try w.writeAll("}}");
    return buf.items;
}

/// Build a BoardText label on Dwgs.User as an Any-wrapped proto string.
fn boardLabelItem(arena: std.mem.Allocator, x: f64, y: f64, text: []const u8) ![]const u8 {
    var buf: std.ArrayListUnmanaged(u8) = .empty;
    const w = buf.writer(arena);
    try w.writeAll(PROTO_ANY_OPEN ++ PROTO_TYPE_URL_BOARDTEXT ++ "\",\"layer\":\"" ++ PROTO_LAYER_DWGS_USER ++ "\",\"text\":{\"position\":");
    try writeProtoVec2(w, x, y);
    try w.writeAll(",\"attributes\":{\"size\":");
    try writeProtoVec2(w, STAGE_LABEL_SIZE_MM, STAGE_LABEL_SIZE_MM);
    try w.writeAll("},\"text\":");
    try json_writer.writeString(w, text);
    try w.writeAll("}}");
    return buf.items;
}

/// Group buffered section-add indices by section name (sorted for a stable
/// layout) so `emitStagedAdds` can lay each section out as its own box.
fn groupAddsBySection(
    arena: std.mem.Allocator,
    adds: []const PendingAdd,
    buckets: *std.StringHashMap(std.ArrayListUnmanaged(usize)),
    order: *std.ArrayListUnmanaged([]const u8),
) !void {
    for (adds, 0..) |pa, i| {
        const key = if (pa.section.len > 0) pa.section else "(unsectioned)";
        const e = try buckets.getOrPut(key);
        if (!e.found_existing) {
            e.value_ptr.* = .empty;
            try order.append(arena, key);
        }
        try e.value_ptr.append(arena, i);
    }
    std.mem.sort([]const u8, order.items, {}, lessThanStr);
}

/// Emit every buffered `add`, choosing each part's placement. When the design
/// has a premade placement-tool layout (`premade_layout`) that names the part,
/// it lands at that exact (x, y, rot) so a first-time import reproduces the
/// tool's layout — no staging box, and since the sync only ever writes
/// footprints, no traces or vias. Parts the layout doesn't name (or all parts,
/// when there is no layout) fall back to the section-staging grid below.
/// Existing/matched footprints are never moved either way.
fn emitStagedAdds(d: *DiffContext, w: anytype, first: *bool) !void {
    const arena = d.spc.arena;
    const adds = d.pending_adds.items;
    if (adds.len == 0) return;

    const layout = d.premade_layout orelse return emitStagingGrid(d, w, first, adds);

    // Place every layout-named part at its exact pose; collect the rest for the
    // staging grid so a partial layout still lands its unknown parts somewhere.
    var leftover: std.ArrayListUnmanaged(PendingAdd) = .empty;
    for (adds) |pa| {
        const pose = layout.get(pa.inst.ref_des) orelse {
            try leftover.append(arena, pa);
            continue;
        };
        const kmod = loadKicadMod(d.spc, pa.fp_name, pa.inst.component) orelse continue;
        const fp_def = loadFootprintDefForInstance(d.spc, pa.fp_name, pa.inst, pa.canopy_net, pa.section);
        try emitAddOp(w, first, pa.inst, pa.fp_name, kmod, fp_def, d.pad_net_map, mmToNm(pose.x), mmToNm(pose.y), pose.rot, pa.canopy_net, pa.section);
        d.summary.added += 1;
    }
    return emitStagingGrid(d, w, first, leftover.items);
}

/// Lay `adds` out as per-section clusters in a staging area and emit them: one
/// positioned `add` op per part plus a `create_board_item` box rect + text
/// label per section. Positions come from the diff-independent `staging_layout`
/// (built from the whole design), so a part lands at the same coordinate on
/// every push — a smaller follow-up push reuses the same cells and box size
/// instead of re-flowing fresh boxes over the previous push's. Parts with no
/// footprint source are skipped, and a section whose parts are all skipped
/// draws no (empty) box.
fn emitStagingGrid(d: *DiffContext, w: anytype, first: *bool, adds: []const PendingAdd) !void {
    const arena = d.spc.arena;
    if (adds.len == 0) return;

    var buckets = std.StringHashMap(std.ArrayListUnmanaged(usize)).init(arena);
    var order: std.ArrayListUnmanaged([]const u8) = .empty;
    try groupAddsBySection(arena, adds, &buckets, &order);

    // Uniform box size from the largest section across the *whole design* (not
    // just this push), so the box dimensions are identical on every sync.
    const ucols = boxCols(d.staging_layout.max_n);
    const urows = (d.staging_layout.max_n + ucols - 1) / ucols;
    const box_w = 2 * STAGE_BOX_PAD_MM + @as(f64, @floatFromInt(ucols)) * STAGE_PART_PITCH_MM;
    const box_h = STAGE_LABEL_H_MM + 2 * STAGE_BOX_PAD_MM + @as(f64, @floatFromInt(urows)) * STAGE_PART_PITCH_MM;
    const pitch_x = box_w + STAGE_BOX_GAP_X_MM;
    const pitch_y = box_h + STAGE_BOX_GAP_Y_MM;

    for (order.items) |sec| {
        const idxs = (buckets.get(sec) orelse continue).items;
        if (idxs.len == 0) continue;
        // Each section owns a fixed grid cell (`gi`), shared by all its parts.
        const gi = (d.staging_layout.by_ref.get(adds[idxs[0]].inst.ref_des) orelse StagingSlot{ .gi = 0, .slot = 0 }).gi;
        const cell_x = STAGE_ORIGIN_X_MM + @as(f64, @floatFromInt(gi % STAGE_SECTIONS_PER_ROW)) * pitch_x;
        const cell_y = STAGE_ORIGIN_Y_MM + @as(f64, @floatFromInt(gi / STAGE_SECTIONS_PER_ROW)) * pitch_y;

        var emitted: usize = 0;
        for (idxs) |idx| {
            const pa = adds[idx];
            const kmod = loadKicadMod(d.spc, pa.fp_name, pa.inst.component) orelse continue;
            const fp_def = loadFootprintDefForInstance(d.spc, pa.fp_name, pa.inst, pa.canopy_net, pa.section);
            const slot = (d.staging_layout.by_ref.get(pa.inst.ref_des) orelse StagingSlot{ .gi = 0, .slot = 0 }).slot;
            const col: f64 = @floatFromInt(slot % ucols);
            const row: f64 = @floatFromInt(slot / ucols);
            const px = cell_x + STAGE_BOX_PAD_MM + (col + STAGE_HALF) * STAGE_PART_PITCH_MM;
            const py = cell_y + STAGE_LABEL_H_MM + STAGE_BOX_PAD_MM + (row + STAGE_HALF) * STAGE_PART_PITCH_MM;
            try emitAddOp(w, first, pa.inst, pa.fp_name, kmod, fp_def, d.pad_net_map, mmToNm(px), mmToNm(py), 0, pa.canopy_net, pa.section);
            d.summary.added += 1;
            emitted += 1;
        }
        if (emitted > 0) {
            const items = [_][]const u8{
                try boardRectItem(arena, cell_x, cell_y, cell_x + box_w, cell_y + box_h),
                try boardLabelItem(arena, cell_x + box_w * STAGE_HALF, cell_y + STAGE_LABEL_H_MM * STAGE_HALF, sec),
            };
            for (items) |item_json| {
                if (!first.*) try w.*.writeAll(",");
                first.* = false;
                try w.*.writeAll(BOARD_ITEM_OP_OPEN);
                try w.*.writeAll(item_json);
                try w.*.writeAll("}");
            }
        }
    }
}

fn emitSwapOp(
    w: anytype,
    first: *bool,
    uuid: []const u8,
    fp_name: []const u8,
    kmod: []const u8,
    fp_def_json: ?[]const u8,
    ref_des: []const u8,
    pad_net_map: *std.StringHashMap([]const u8),
) !void {
    if (!first.*) try w.*.writeAll(",");
    first.* = false;
    try w.*.writeAll("{\"op\":\"swap_footprint\",\"uuid\":");
    try json_writer.writeString(w.*, uuid);
    try w.*.writeAll(",\"new_footprint_name\":");
    try json_writer.writeString(w.*, fp_name);
    try w.*.writeAll(",\"kicad_mod\":");
    try json_writer.writeString(w.*, kmod);
    if (fp_def_json) |def| {
        try w.*.writeAll(",\"footprint_def\":");
        try w.*.writeAll(def);
    }
    try w.*.writeAll(",\"pad_nets\":");
    try writePadNetsArray(w.*, ref_des, pad_net_map);
    try w.*.writeAll("}");
}

/// Emit one `set_pad_net` op per pad whose net assignment differs from
/// what the client reported. Pads only on one side (e.g. NC pads or pads
/// the client didn't list) are skipped. Returns the number of ops emitted
/// so the caller can decide whether the surrounding instance counts as
/// "updated" — a no-diff sync should be silent in the user-facing toast.
fn emitPadNetOps(
    d: *DiffContext,
    w: anytype,
    first: *bool,
    uuid: []const u8,
    ref_des: []const u8,
    pad_net_map: *std.StringHashMap([]const u8),
    client_pads: []const PadAssign,
) !u32 {
    var key_buf: [256]u8 = undefined;
    var ck_buf: [256]u8 = undefined;
    var emitted: u32 = 0;
    for (client_pads) |cp| {
        const key = std.fmt.bufPrint(&key_buf, "{s}|{s}", .{ ref_des, cp.number }) catch continue;
        if (pad_net_map.get(key)) |want| {
            if (std.mem.eql(u8, want, cp.net)) continue;
            if (try emitOpUnlessApplied(d, w, first, "set_pad_net", .{
                .{ "uuid", uuid },
                .{ "pad", cp.number },
                .{ "net", want },
            }, uuid, cp.number, want)) {
                emitted += 1;
            }
        } else if (cp.net.len > 0) {
            // The design no longer assigns this pad, yet the board pad still
            // carries a net. Clear it only when that net is a single-pad signal
            // the design now puts on a different pad of this instance (a moved
            // pin) — never a multi-pad GND/power net or a net the design
            // doesn't know, which are left untouched.
            const ck = std.fmt.bufPrint(&ck_buf, "{s}|{s}", .{ ref_des, cp.net }) catch continue;
            if ((d.net_pad_count.get(ck) orelse 0) != 1) continue;
            if (try emitOpUnlessApplied(d, w, first, "set_pad_net", .{
                .{ "uuid", uuid },
                .{ "pad", cp.number },
                .{ "net", "" },
            }, uuid, cp.number, "")) {
                emitted += 1;
            }
        }
    }
    return emitted;
}

fn writePadNetsArray(
    w: anytype,
    ref_des: []const u8,
    pad_net_map: *std.StringHashMap([]const u8),
) !void {
    try w.writeAll("[");
    var it = pad_net_map.iterator();
    var first = true;
    while (it.next()) |entry| {
        // Keys have shape "ref|pin"; filter to this ref_des.
        const k = entry.key_ptr.*;
        const sep = std.mem.indexOfScalar(u8, k, '|') orelse continue;
        if (!std.mem.eql(u8, k[0..sep], ref_des)) continue;
        if (!first) try w.writeAll(",");
        first = false;
        try w.writeAll("[");
        try json_writer.writeString(w, k[sep + 1 ..]);
        try w.writeAll(",");
        try json_writer.writeString(w, entry.value_ptr.*);
        try w.writeAll("]");
    }
    try w.writeAll("]");
}

