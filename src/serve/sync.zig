/// Group buffered section-add indices by section name (sorted for a stable
/// layout) so `emitStagedAdds` can lay each section out as its own box.
fn groupAddsBySection(
    arena: std.mem.Allocator,
    adds: []const PendingAdd,
    buckets: *std.StringHashMapUnmanaged(std.ArrayList(usize)),
    order: *std.ArrayList([]const u8),
) !void {
    for (adds, 0..) |pa, i| {
        const key = if (pa.section.len > 0) pa.section else "(unsectioned)";
        const e = try buckets.getOrPut(arena, key);
        if (!e.found_existing) {
            e.value_ptr.* = .empty;
            try order.append(arena, key);
        }
        try e.value_ptr.append(arena, i);
    }
    std.mem.sort([]const u8, order.items, {}, lessThanStr);
}

/// Emit every buffered `add`, choosing each part's placement in three tiers:
///   1. **Per-sub-block module layout** — a not-yet-placed sub-block is seeded
///      from *its own* module default layout, as one pre-laid-out unit in the
///      staging area (drag into place in KiCad). See `seedSubBlocks`.
///   2. **Whole-design premade layout** (fresh boards only — see
///      `DiffContext.board_fresh`) — a remaining part the design's own
///      layout names lands at that exact (x, y, rot).
///   3. **Section-staging grid** — anything left clusters in a staging box.
/// Existing/matched footprints are never moved by any tier.
///
/// When the request carries an explicit sub-circuit selection (the Push modal's
/// checkboxes — `seed_groups`/`seed_all`), that path takes over instead: the
/// selected groups are seeded from the saved whole-design layout (anchored on
/// their main IC if it's already on the board, else an off-board box, see
/// `seedSelectedGroups`) and everything else falls straight to the staging
/// grid. The default (no selection) flow above is left byte-for-byte unchanged.
fn emitStagedAdds(d: *DiffContext, w: anytype, first: *bool) !void {
    const arena = d.spc.arena;
    const adds = d.pending_adds.items;
    if (adds.len == 0) return;

    var leftover: std.ArrayList(PendingAdd) = .empty;

    // Explicit selection: seed the chosen sub-circuits from the saved layout,
    // send the rest (unselected groups + parts the layout doesn't name) to the
    // plain staging grid. Bypasses the module-layout / board_fresh tiers — the
    // user has asked for the whole-design layout regardless of board state.
    // `planSelectedGroups` decides placements; the writing stays here (it owns
    // the ops writer) so the helpers need no `anytype` writer param.
    if (d.seed_groups != null or d.seed_all) {
        const plan = try planSelectedGroups(d, adds, &leftover);
        for (plan.placed) |p| {
            const pa = p.pa;
            const kmod = loadKicadMod(d.spc, pa.fp_name, pa.inst.component) orelse continue;
            const fp_def = loadFootprintDefForInstance(d.spc, pa.fp_name, pa.inst, pa.canopy_net, pa.section);
            const ap = AddPose{ .x_nm = mmToNm(p.x_mm), .y_nm = mmToNm(p.y_mm), .rot_deg = p.rot, .back = p.back };
            try emitAddOp(w, first, pa.inst, pa.fp_name, kmod, fp_def, d.pad_net_map, ap, pa.canopy_net, pa.section);
            d.summary.added += 1;
        }
        // One off-board box per seeded sub-circuit: a labelled rectangle around
        // it + a KiCad `(group …)` op so each module's parts drag as their own
        // unit, referenced by their canopy_uuids (= the footprints' KiCad uuids
        // for fresh adds). The shared offset already preserves inter-block
        // geometry; the per-box framing just makes each sub-circuit draggable.
        for (plan.boxes) |b| {
            const items = [_][]const u8{
                try boardRectItem(arena, b.x0, b.y0, b.x1, b.y1),
                try boardLabelItem(arena, (b.x0 + b.x1) * stage_half, b.y0 + stage_label_h_mm * stage_half, b.label),
            };
            for (items) |item_json| {
                if (!first.*) try w.*.writeAll(",");
                first.* = false;
                try w.*.writeAll(board_item_op_open);
                try w.*.writeAll(item_json);
                try w.*.writeAll("}");
            }
            if (b.members.len > 0) {
                if (!first.*) try w.*.writeAll(",");
                first.* = false;
                try w.*.writeAll("{\"op\":\"group\",\"name\":");
                try json_writer.writeString(w.*, b.label);
                try w.*.writeAll(",\"members\":[");
                for (b.members, 0..) |m, i| {
                    if (i > 0) try w.*.writeAll(",");
                    try json_writer.writeString(w.*, m);
                }
                try w.*.writeAll("]}");
            }
        }
        // Carry each seeded sub-circuit's module routing onto the board, offset to
        // sit under its footprints (the whole-design via path is skipped when any
        // block seeds — see the emitLayoutVias call site).
        try emitSeedCopper(d, w, first, plan.copper);
        return emitStagingGrid(d, w, first, leftover.items);
    }

    // Tier 1: seed sub-blocks that aren't on the board from their module layouts.
    try seedSubBlocks(d, w, first, adds, &leftover);
    const rest = leftover.items;
    if (rest.len == 0) return;

    // Tier 2: the whole-design premade layout names the part → exact pose.
    // Fresh boards only — on a populated board the layout's coordinates land
    // in the middle of existing placement/routing, so everything left over
    // goes to the off-board staging grid instead.
    const layout_if_fresh = if (d.board_fresh) d.premade_layout else null;
    const layout = layout_if_fresh orelse return emitStagingGrid(d, w, first, rest);
    var grid_rest: std.ArrayList(PendingAdd) = .empty;
    for (rest) |pa| {
        const pose = layout.get(pa.inst.ref_des) orelse {
            try grid_rest.append(arena, pa);
            continue;
        };
        const kmod = loadKicadMod(d.spc, pa.fp_name, pa.inst.component) orelse continue;
        const fp_def = loadFootprintDefForInstance(d.spc, pa.fp_name, pa.inst, pa.canopy_net, pa.section);
        const ap = AddPose{ .x_nm = mmToNm(pose.x), .y_nm = mmToNm(pose.y), .rot_deg = pose.rot, .back = pose.side == .bottom };
        try emitAddOp(w, first, pa.inst, pa.fp_name, kmod, fp_def, d.pad_net_map, ap, pa.canopy_net, pa.section);
        d.summary.added += 1;
    }
    // Tier 3: grid staging.
    return emitStagingGrid(d, w, first, grid_rest.items);
}

// ── Explicit per-sub-circuit layout seeding (Push modal checkboxes) ──────────

/// The chosen anchor of a sub-circuit group plus whether it is already on the
/// target board. The anchor is the group's "main part" (highest `anchorRank`,
/// ties → lowest ref-des); when it's on the board its live position lets
/// `seedSelectedGroups` re-centre the group's saved layout there (the IC stays
/// put, the rest of the sub-circuit flows in around it). `ref` is the flattened
/// ref-des used to look the anchor's saved pose up in the whole-design layout;
/// `origin_key` is the anchor instance's stable module-local key, used to look
/// it up in a module's origin-key-keyed layout (see `SubCircuitSrc.is_module`)
/// even when the anchor is already placed and thus absent from the pending adds.
const GroupAnchor = struct {
    ref: []const u8,
    origin_key: []const u8 = "",
    on_board: bool = false,
    board_x: f64 = 0,
    board_y: f64 = 0,
    /// The anchor footprint's netlisp-model rotation + side as placed on the
    /// board (decoded from its `(at)` angle + layer via `kicadRotToNetlisp`).
    /// `placeOneSelected` composes these into the group transform so a seeded
    /// sub-circuit follows a rotated / bottom-side anchor, not just its position.
    board_rot: f64 = 0,
    board_back: bool = false,
};

/// Rank a flattened ref-des as a placement anchor by its prefix letter: an IC
/// (`U`) outranks a connector / large hub (`J`/`P`/`X`/`Q`), which outranks any
/// passive or other part. Classifies the leaf after the last `/` so a sub-block
/// part (`mcu/U12`) is judged on `U12`, not the sub-block name.
fn anchorRank(ref_des: []const u8) u8 {
    const leaf = if (std.mem.lastIndexOfScalar(u8, ref_des, '/')) |i| ref_des[i + 1 ..] else ref_des;
    if (leaf.len == 0) return 0;
    return switch (leaf[0]) {
        'U' => 3,
        'J', 'P', 'X', 'Q' => 2,
        else => 1,
    };
}

/// Pick each sub-circuit group's anchor (see `GroupAnchor`); resolve whether it's
/// on the board by sharing `d`'s indexes so the match runs the diff loop's relink
/// tiers. Groups are `sectionForRef`'s keys; live pos from `board_positions`.
fn computeGroupAnchors(
    arena: std.mem.Allocator,
    instances: []const export_kicad.FlatInstance,
    d: *const DiffContext,
    board_positions: ?*const std.StringHashMapUnmanaged(FpPlacement),
) !std.StringHashMapUnmanaged(GroupAnchor) {
    var out = std.StringHashMapUnmanaged(GroupAnchor).empty;
    var rank_of = std.StringHashMapUnmanaged(u8).empty;
    for (instances) |inst| {
        const sec = sectionForRef(inst.ref_des, d.ref_to_section);
        if (sec.len == 0) continue;
        const rank = anchorRank(inst.ref_des);
        const better = blk: {
            const prev = rank_of.get(sec) orelse break :blk true;
            if (rank != prev) break :blk rank > prev;
            // Equal rank → keep the lexicographically smaller ref (deterministic).
            const cur = out.get(sec) orelse break :blk true;
            break :blk std.mem.lessThan(u8, inst.ref_des, cur.ref);
        };
        if (!better) continue;
        var ga = GroupAnchor{ .ref = inst.ref_des, .origin_key = inst.origin_key };
        // Diff loop's own relink tiers (matchInstance): netsig/uuid/ref/migration.
        const bfp: ?BoardFp = d.by_netsig.get(inst.uuid) orelse d.by_uuid.get(inst.uuid) orelse
            d.by_ref.get(inst.ref_des) orelse d.by_migration.get(inst.uuid);
        if (bfp) |m| {
            if (board_positions) |bp| {
                if (m.kicad_uuid.len > 0) {
                    if (bp.get(m.kicad_uuid)) |p| {
                        ga.on_board = true;
                        ga.board_x = p.x;
                        ga.board_y = p.y;
                        ga.board_back = std.mem.startsWith(u8, p.layer, "B.");
                        ga.board_rot = kicadRotToNetlisp(p.rot, ga.board_back);
                    }
                }
            }
        }
        try rank_of.put(arena, sec, rank);
        try out.put(arena, sec, ga);
    }
    return out;
}

/// Whether sub-circuit group `sec` was selected for layout seeding.
fn groupSelected(d: *DiffContext, sec: []const u8) bool {
    if (d.seed_all) return true;
    const set = d.seed_groups orelse return false;
    return set.contains(sec);
}

/// One fresh add placed at an absolute board coordinate (mm) by the seeding
/// planner. `emitStagedAdds` turns each into an `add` op (it owns the writer).
const PositionedAdd = struct { pa: PendingAdd, x_mm: f64, y_mm: f64, rot: f64, back: bool = false };
/// A group add with its already-resolved pre-offset board position (mm + rot)
/// and its sub-circuit name (the `sectionForRef` key — the sub-block slug, e.g.
/// `mcu`). The position is the part's source pose (module ★ layout for a
/// sub-block, else the parent cache) re-anchored so the sub-circuit's IC lands
/// at its parent-cache spot; `finishOffBoardBlock` then applies ONE shared
/// off-board offset over the whole block and bins these into one box + group
/// per sub-circuit.
const NamedAdd = struct { pa: PendingAdd, bx: f64, by: f64, rot: f64, sec: []const u8 };
/// One off-board labelled box (mm) bounding a single seeded sub-circuit whose IC
/// is NOT already on the board, plus that sub-circuit's canopy_uuids so the
/// writer binds them into their own draggable KiCad `(group …)`. `emitStagedAdds`
/// draws the rect/label + group op once per box.
const SeedBox = struct { x0: f64, y0: f64, x1: f64, y1: f64, label: []const u8, members: []const []const u8 };
/// One seeded sub-circuit's module copper to carry onto the board: the module ★
/// snapshot's routes (module-local mm + module-local net names), the module → parent
/// net-name bridge, the sub-block slug (for the private-net `<slug>/NET` fallback),
/// and the rigid `xform` applied to that sub-circuit's parts — so `emitSeedCopper`
/// transforms the copper by the SAME transform the footprints got (mirroring +
/// F.Cu↔B.Cu layer swap on a flip, a pure translation on the common same-side case).
const SeedCopper = struct {
    routes: pcb_layout.SavedRoutes,
    net_map: std.StringHashMapUnmanaged([]const u8),
    slug: []const u8,
    xform: Pose2D,
};
/// The output collectors `placeOneSelected` appends into, bundled so its
/// signature stays small: `leftover` (parts the source doesn't name), `placed`
/// (on-board positioned adds), `block` (off-board parts awaiting the shared
/// offset), and the two copper buckets — `copper` (on-board, final offset) and
/// `block_copper` (off-board, the shared block offset added in `planSelectedGroups`).
const SeedAccum = struct {
    leftover: *std.ArrayList(PendingAdd),
    placed: *std.ArrayList(PositionedAdd),
    block: *std.ArrayList(NamedAdd),
    copper: *std.ArrayList(SeedCopper),
    block_copper: *std.ArrayList(SeedCopper),
};
/// The seeding planner's output: positioned adds, one off-board box per seeded
/// sub-circuit (empty when every selected group anchored on the board), and the
/// per-sub-circuit module copper to lay down (empty when no seeded sub-block had
/// routed copper).
const SeedPlan = struct { placed: []const PositionedAdd, boxes: []const SeedBox, copper: []const SeedCopper };
/// `finishOffBoardBlock`'s result: the per-sub-circuit labelled boxes plus the ONE
/// shared (dx, dy) mm offset applied to the whole off-board block — so its copper
/// can be shifted by the identical amount its parts were.
const OffBoardResult = struct { boxes: []const SeedBox, dx: f64, dy: f64 };

/// Fallback box label for off-board seeded parts that carry no sub-circuit name
/// (top-level parts not inside a sub-block).
const seed_box_label = "Seeded layout — drag onto board";

/// The pose source for one selected sub-circuit. A sub-block with its OWN saved
/// module layout (`lib/modules/<m>.layouts.json`, ★-default) reads THAT so the
/// module reproduces exactly what the user starred for it: `is_module = true`,
/// the map is keyed by stable `origin_key` (matched per part on
/// `FlatInstance.origin_key`, which survives `(hierarchical-ids)` renumbering).
/// Everything else (sections, sub-blocks with no module layout) falls back to
/// the parent design cache: `is_module = false`, keyed by full flattened ref.
/// A module source also carries the snapshot's routed copper (`routes`) and the
/// module-pin → net bridge (`pin_nets`), so `emitSeedCopper` can lay the module's
/// hand routing onto the board; both are empty on the parent-cache path.
const SubCircuitSrc = struct {
    map: std.StringHashMapUnmanaged(pcb_layout.SyncPose),
    is_module: bool,
    routes: ?pcb_layout.SavedRoutes = null,
    pin_nets: []const pcb_layout.SubPinNet = &.{},
};

/// Resolve the pose source for sub-circuit `sec` (see `SubCircuitSrc`). Null when
/// neither a module layout nor the parent cache can supply poses.
fn subCircuitSource(d: *DiffContext, sec: []const u8) !?SubCircuitSrc {
    if (subBlockNamed(d.sub_blocks, sec)) |sb| {
        // Module layout keyed by origin_key (survives the parent's ref-des
        // renumbering), matched per part on FlatInstance.origin_key.
        if (pcb_layout.subBlockPoseByOriginKey(d.spc.arena, d.spc.project_dir, sb)) |s|
            return .{ .map = s.map, .is_module = true, .routes = s.routes, .pin_nets = s.pin_nets };
    }
    if (d.premade_layout) |cache| return .{ .map = cache, .is_module = false };
    return null;
}

/// Plan placement for an explicit sub-circuit selection (the Push modal). Each
/// selected sub-circuit's freshly-added parts take their *source* poses — the
/// sub-block's own starred module layout when it has one, else the parent design
/// cache (`subCircuitSource`) — re-anchored so the sub-circuit's main IC lands on
/// its target:
///   • IC already on the board ⇒ parts flow in AROUND that IC's live position
///     (the IC stays put), placed directly on the board;
///   • otherwise ⇒ the IC lands at its parent-cache spot and the parts join one
///     shared OFF-BOARD block.
/// So the off-board block keeps the whole board's INTER-block geometry (every
/// module sits where the design solve put it) while each module keeps its OWN
/// starred INTERNAL arrangement — and within that shared frame each sub-circuit
/// gets its own labelled box + draggable KiCad group. Parts no source names, and
/// non-selected groups, go to `leftover` for the caller's staging grid. Only
/// fresh adds get positions (existing footprints never move); no GND vias are
/// planned. Result lives on `arena`.
fn planSelectedGroups(
    d: *DiffContext,
    adds: []const PendingAdd,
    leftover: *std.ArrayList(PendingAdd),
) !SeedPlan {
    const arena = d.spc.arena;
    var placed: std.ArrayList(PositionedAdd) = .empty;
    var buckets = std.StringHashMapUnmanaged(std.ArrayList(usize)).empty;
    var order: std.ArrayList([]const u8) = .empty;
    try groupAddsBySection(arena, adds, &buckets, &order);

    // Off-board parts accumulate here (pre-offset board coords); the whole block
    // then shifts clear of the board with ONE shared offset.
    var block: std.ArrayList(NamedAdd) = .empty;
    var copper: std.ArrayList(SeedCopper) = .empty;
    var block_copper: std.ArrayList(SeedCopper) = .empty;
    var acc = SeedAccum{
        .leftover = leftover,
        .placed = &placed,
        .block = &block,
        .copper = &copper,
        .block_copper = &block_copper,
    };
    for (order.items) |sec| {
        const idxs = (buckets.get(sec) orelse continue).items;
        if (!groupSelected(d, sec)) {
            for (idxs) |idx| try leftover.append(arena, adds[idx]);
            continue;
        }
        const src = (try subCircuitSource(d, sec)) orelse {
            for (idxs) |idx| try leftover.append(arena, adds[idx]);
            continue;
        };
        try placeOneSelected(d, sec, idxs, adds, src, d.group_anchors.get(sec), &acc);
    }
    const ob = try finishOffBoardBlock(arena, block.items, &placed);
    d.summary.blocks += @intCast(ob.boxes.len);
    // The off-board block shifted by ONE shared offset; fold it into each of its
    // sub-circuits' copper (their per-group anchor offset was recorded already).
    for (block_copper.items) |c| {
        try copper.append(arena, .{
            .routes = c.routes,
            .net_map = c.net_map,
            .slug = c.slug,
            .xform = c.xform.translated(ob.dx, ob.dy),
        });
    }
    return .{ .placed = placed.items, .boxes = ob.boxes, .copper = copper.items };
}

/// A rigid 2-D placement transform in the netlisp pose model — the algebra a
/// seeded sub-circuit uses to follow its anchor IC onto a rotated / bottom-side
/// board pose. Maps a footprint-local point v to world as
/// `apply(v) = rotate(mirrorX(v)) + (tx,ty)`, where `mirrorX` negates x iff
/// `mirror` and `rotate` is the {0,90,180,270} matrix of `optimizer.rotateLocal`
/// applied in the netlisp y-down frame. A part's `SyncPose` (x,y,rot,side) is
/// exactly such a transform (mirror ⟺ side==bottom) — the mirror-before-rotate
/// order matches `optimizer.worldPt`, the authoritative pose→pad-world math, so
/// composing these preserves every pad's relative position under a flip.
const Pose2D = struct {
    tx: f64 = 0,
    ty: f64 = 0,
    rot: f64 = 0,
    mirror: bool = false,

    /// `optimizer.rotateLocal`'s CCW {0,90,180,270} matrix (the netlisp y-down
    /// frame). Non-right angles fall through to identity — module poses only
    /// ever carry the four right angles.
    fn rotate(deg: f64, x: f64, y: f64) [2]f64 {
        return switch (numeric.checkedInt(i64, @round(@mod(deg, 360.0))) orelse 0) {
            90 => .{ -y, x },
            180 => .{ -x, -y },
            270 => .{ y, -x },
            else => .{ x, y },
        };
    }

    /// Linear part only (rotate∘mirrorX, no translation).
    fn applyLin(self: Pose2D, x: f64, y: f64) [2]f64 {
        return rotate(self.rot, if (self.mirror) -x else x, y);
    }

    /// World point of footprint-local (x,y).
    fn apply(self: Pose2D, x: f64, y: f64) [2]f64 {
        const l = self.applyLin(x, y);
        return .{ l[0] + self.tx, l[1] + self.ty };
    }

    /// `self ∘ other` — apply `other` first, then `self`. Mirror XORs; rotation
    /// adds, negated by `self`'s mirror (since Mir·R(θ)=R(−θ)·Mir); translation
    /// is `self`'s linear part on `other`'s translation plus `self`'s own.
    fn compose(self: Pose2D, other: Pose2D) Pose2D {
        const t = self.applyLin(other.tx, other.ty);
        return .{
            .tx = t[0] + self.tx,
            .ty = t[1] + self.ty,
            .rot = @mod(self.rot + (if (self.mirror) -other.rot else other.rot), 360.0),
            .mirror = self.mirror != other.mirror,
        };
    }

    /// The inverse transform: `inverse() ∘ self == identity`.
    fn inverse(self: Pose2D) Pose2D {
        var out = Pose2D{ .rot = @mod(if (self.mirror) self.rot else -self.rot, 360.0), .mirror = self.mirror };
        const t = out.applyLin(self.tx, self.ty);
        out.tx = -t[0];
        out.ty = -t[1];
        return out;
    }

    /// Post-compose a pure translation (shift the transform's output by dx,dy) —
    /// how the off-board block's one shared offset folds into its copper transform.
    fn translated(self: Pose2D, dx: f64, dy: f64) Pose2D {
        return .{ .tx = self.tx + dx, .ty = self.ty + dy, .rot = self.rot, .mirror = self.mirror };
    }
};

/// A module ★-layout pose as a `Pose2D` (mirror ⟺ bottom side).
fn poseFromSync(sp: pcb_layout.SyncPose) Pose2D {
    return .{ .tx = sp.x, .ty = sp.y, .rot = sp.rot, .mirror = sp.side == .bottom };
}

/// The rigid transform that carries a seeded sub-circuit from its module ★
/// layout onto the board so its anchor IC lands on the anchor's LIVE board pose
/// — position, rotation, AND side: `board_anchor ∘ inverse(module_anchor)`.
/// Applying it to every part's module pose reproduces the module's internal
/// geometry exactly while flipping/rotating the whole group to match a
/// bottom-side or rotated anchor. Reduces to a pure translation (rot 0, no
/// mirror) when the anchor keeps the module's side and rotation — the common
/// same-side case, byte-identical to the old `target − src_anchor` offset.
fn groupTransform(module_anchor: pcb_layout.SyncPose, a: GroupAnchor) Pose2D {
    const board = Pose2D{ .tx = a.board_x, .ty = a.board_y, .rot = a.board_rot, .mirror = a.board_back };
    return board.compose(poseFromSync(module_anchor).inverse());
}

/// Place one selected sub-circuit's parts from its `src` poses, re-anchored so
/// the sub-circuit's IC lands on its target. When the IC is already on the board
/// the whole group rides a rigid `groupTransform` (position + rotation + side)
/// onto the board, so a bottom-side or rotated anchor takes its passives (and
/// copper) with it — the same-side, zero-rotation case reduces to the old pure
/// translation. Otherwise the IC lands at its parent-cache spot and the parts
/// join the shared OFF-BOARD block (top-side, translate-only, unchanged). Parts
/// the source doesn't name go to `leftover`.
fn placeOneSelected(
    d: *DiffContext,
    sec: []const u8,
    idxs: []const usize,
    adds: []const PendingAdd,
    src: SubCircuitSrc,
    anchor: ?GroupAnchor,
    acc: *SeedAccum,
) !void {
    const arena = d.spc.arena;
    var offx: f64 = 0;
    var offy: f64 = 0;
    var xform: Pose2D = .{}; // module-world → board-world (the on-board rigid transform)
    var on_board = false;
    if (anchor) |a| {
        // A module source is keyed by origin_key, so key the anchor by its own
        // `origin_key` (resolved from the flattened roster in
        // `computeGroupAnchors`). Its flattened ref-des won't key the map, and
        // this works even when the anchor IC is already placed — and thus absent
        // from `adds` — which is exactly when its passives must centre on it.
        const akey = if (src.is_module) a.origin_key else a.ref;
        if (src.map.get(akey)) |as_| {
            if (a.on_board) {
                on_board = true;
                xform = groupTransform(as_, a);
            } else if (d.premade_layout) |cache| if (cache.get(a.ref)) |cp| {
                offx = cp.x - as_.x;
                offy = cp.y - as_.y;
            };
        }
    }
    for (idxs) |idx| {
        const pa = adds[idx];
        const key = if (src.is_module) pa.inst.origin_key else pa.inst.ref_des;
        const sp = src.map.get(key) orelse {
            try acc.leftover.append(arena, pa);
            continue;
        };
        if (on_board) {
            const np = xform.compose(poseFromSync(sp));
            try acc.placed.append(arena, .{ .pa = pa, .x_mm = np.tx, .y_mm = np.ty, .rot = np.rot, .back = np.mirror });
        } else {
            try acc.block.append(arena, .{ .pa = pa, .bx = sp.x + offx, .by = sp.y + offy, .rot = sp.rot, .sec = sec });
        }
    }
    if (on_board) d.summary.blocks += 1;
    // Carry the module's routed copper along under the SAME transform its parts
    // got — the module-local coordinates share the pose frame. On-board copper
    // rides the rigid transform (mirroring endpoints + swapping F.Cu↔B.Cu when
    // flipped); off-board copper is a pure translation that still owes the shared
    // block offset, folded in by `planSelectedGroups`.
    if (src.is_module) if (src.routes) |sr| {
        const net_map = try subCircuitNetMap(d, src.pin_nets, idxs, adds);
        const cop_xform = if (on_board) xform else Pose2D{ .tx = offx, .ty = offy };
        const job = SeedCopper{ .routes = sr, .net_map = net_map, .slug = sec, .xform = cop_xform };
        try (if (on_board) acc.copper else acc.block_copper).append(arena, job);
    };
}

/// Map each of a seeded sub-circuit's module-local net names to the parent net
/// its pads carry on the board, so `emitSeedCopper` lays the copper on the same
/// net as the pads it connects. For each module pin sample `(net, origin_key,
/// pad)` the parent ref is `origin_key → ref` (the sub-circuit's freshly-added
/// parts — every net the seeded copper touches has at least one fresh pad), and
/// the parent net is that pad's display net from `d.pad_net_map` ("ref|pin") — the
/// exact spelling the netlist sync writes on the pad, so copper and pad agree even
/// when a bare name collided and kept its `<slug>/…` prefix. Unresolved nets fall
/// back to the `<slug>/NET` flatten spelling in `bridgedCopperNet`.
fn subCircuitNetMap(
    d: *DiffContext,
    pin_nets: []const pcb_layout.SubPinNet,
    idxs: []const usize,
    adds: []const PendingAdd,
) !std.StringHashMapUnmanaged([]const u8) {
    const arena = d.spc.arena;
    var ok_ref = std.StringHashMapUnmanaged([]const u8).empty;
    for (idxs) |i| {
        const inst = adds[i].inst;
        if (inst.origin_key.len == 0) continue;
        try ok_ref.put(arena, inst.origin_key, inst.ref_des);
    }
    var net_map = std.StringHashMapUnmanaged([]const u8).empty;
    for (pin_nets) |ps| {
        if (net_map.contains(ps.net)) continue;
        const dref = ok_ref.get(ps.origin_key) orelse continue;
        const key = try std.fmt.allocPrint(arena, "{s}|{s}", .{ dref, ps.pad });
        const dn = d.pad_net_map.get(key) orelse continue;
        try net_map.put(arena, ps.net, dn);
    }
    return net_map;
}

/// The parent-board net spelling for a module-local copper net: the bridged
/// parent display net when a module pin resolved (`net_map`), else the
/// `<slug>/NET` flatten spelling a module-private net takes in the parent netlist
/// (mirrors `pcb_layout_page.mappedNet`). Empty string ⇒ skip (unnamed copper).
fn bridgedCopperNet(
    arena: std.mem.Allocator,
    net_map: std.StringHashMapUnmanaged([]const u8),
    slug: []const u8,
    net: []const u8,
) []const u8 {
    if (net.len == 0) return "";
    if (net_map.get(net)) |m| return m;
    return std.fmt.allocPrint(arena, "{s}/{s}", .{ slug, net }) catch "";
}

/// KiCad copper-layer name for a router / `SavedTrack` layer index. The router
/// numbers signal layers 0 = top (F.Cu), 1 = bottom (B.Cu); plane-free inner
/// signal layers count up from 2 in stack order, matching `export_gerber.zig`'s
/// `In{i-1}.Cu` naming (2 → In1.Cu, 3 → In2.Cu, …). Verified against
/// `router.Track` ("layer 0 = top, 1 = bottom signal") and
/// `export_gerber.planLayers`. Seeded vias are through-hole, so they need no map.
fn kicadCopperLayer(arena: std.mem.Allocator, l: u8) ![]const u8 {
    return switch (l) {
        0 => "F.Cu",
        1 => "B.Cu",
        else => try std.fmt.allocPrint(arena, "In{d}.Cu", .{l - 1}),
    };
}

/// The router layer index a seeded track/via takes after its group's transform:
/// a flipped group (`mirror`) swaps its OUTER signal layers (F.Cu↔B.Cu, i.e.
/// 0↔1). Inner-layer indices (l≥2) are left as-is — module ★ copper is
/// essentially always outer (l∈{0,1}), and reversing an inner stack needs the
/// board's layer count, which isn't cheaply known here. `kicadCopperLayer` then
/// maps the (possibly swapped) index to its KiCad name.
fn copperLayerSided(l: u8, mirror: bool) u8 {
    if (!mirror) return l;
    return switch (l) {
        0 => 1,
        1 => 0,
        else => l,
    };
}

/// Emit `add_track` / `add_via` ops for each seeded sub-circuit's module copper
/// (`jobs`), applying the job's rigid `xform` to every endpoint/centre — the same
/// transform its footprints got, so a flipped group's copper mirrors and swaps
/// F.Cu↔B.Cu — and bridging module net names to parent nets. Named-only
/// (unbridged, empty-name) copper is skipped. Counted in `d.summary`.
fn emitSeedCopper(d: *DiffContext, w: anytype, first: *bool, jobs: []const SeedCopper) !void {
    const arena = d.spc.arena;
    for (jobs) |job| {
        for (job.routes.tracks) |t| {
            const net = bridgedCopperNet(arena, job.net_map, job.slug, t.net);
            if (net.len == 0) continue;
            const layer = try kicadCopperLayer(arena, copperLayerSided(t.l, job.xform.mirror));
            const p1 = job.xform.apply(t.x1, t.y1);
            const p2 = job.xform.apply(t.x2, t.y2);
            if (!first.*) try w.*.writeAll(",");
            first.* = false;
            try w.*.writeAll("{\"op\":\"add_track\",\"net\":");
            try json_writer.writeString(w.*, net);
            try w.*.print(",\"x1\":{d},\"y1\":{d},\"x2\":{d},\"y2\":{d},\"width\":{d},\"layer\":", .{
                mmToNm(p1[0]), mmToNm(p1[1]),
                mmToNm(p2[0]), mmToNm(p2[1]),
                mmToNm(t.w),
            });
            try json_writer.writeString(w.*, layer);
            try w.*.writeAll("}");
            d.summary.tracks += 1;
        }
        for (job.routes.vias) |v| {
            const net = bridgedCopperNet(arena, job.net_map, job.slug, v.net);
            if (net.len == 0) continue;
            const c = job.xform.apply(v.x, v.y);
            if (!first.*) try w.*.writeAll(",");
            first.* = false;
            try w.*.writeAll(add_via_op_open);
            try json_writer.writeString(w.*, net);
            try w.*.print(via_coords_fmt, .{
                mmToNm(c[0]), mmToNm(c[1]), mmToNm(v.d), mmToNm(v.drill),
            });
            d.summary.vias += 1;
        }
    }
}

/// Translate the whole off-board `block` by ONE shared offset (its collective
/// min-corner → the off-board staging origin), so it reproduces the saved
/// layout's geometry exactly, appending the positioned parts to `placed`. Then
/// emit one labelled `SeedBox` per sub-circuit (`NamedAdd.sec`): each box bounds
/// only that sub-circuit's translated parts and carries only its member uuids,
/// so every module drags as its own KiCad group while keeping its position
/// relative to the others. Boxes come out in first-seen sub-circuit order;
/// returns an empty slice when `block` is empty. Result lives on `arena`.
fn finishOffBoardBlock(
    arena: std.mem.Allocator,
    block: []const NamedAdd,
    placed: *std.ArrayList(PositionedAdd),
) !OffBoardResult {
    if (block.len == 0) return .{ .boxes = &.{}, .dx = 0, .dy = 0 };
    // ONE shared offset from the whole block's collective min-corner.
    var minx: f64 = std.math.floatMax(f64);
    var miny: f64 = std.math.floatMax(f64);
    for (block) |n| {
        minx = @min(minx, n.bx);
        miny = @min(miny, n.by);
    }
    const dx = block_stage_origin_x_mm - minx;
    const dy = block_stage_origin_y_mm - miny;
    for (block) |n| {
        try placed.append(arena, .{ .pa = n.pa, .x_mm = n.bx + dx, .y_mm = n.by + dy, .rot = n.rot });
    }
    // One box + group per sub-circuit, in first-seen order.
    var boxes: std.ArrayList(SeedBox) = .empty;
    var seen = std.StringHashMapUnmanaged(void).empty;
    for (block) |first| {
        if (seen.contains(first.sec)) continue;
        try seen.put(arena, first.sec, {});
        var bx0: f64 = std.math.floatMax(f64);
        var by0: f64 = std.math.floatMax(f64);
        var bx1: f64 = -std.math.floatMax(f64);
        var by1: f64 = -std.math.floatMax(f64);
        var members: std.ArrayList([]const u8) = .empty;
        for (block) |n| {
            if (!std.mem.eql(u8, n.sec, first.sec)) continue;
            const x = n.bx + dx;
            const y = n.by + dy;
            bx0 = @min(bx0, x);
            by0 = @min(by0, y);
            bx1 = @max(bx1, x);
            by1 = @max(by1, y);
            if (n.pa.inst.uuid.len > 0) try members.append(arena, n.pa.inst.uuid);
        }
        try boxes.append(arena, .{
            .x0 = bx0 - stage_box_pad_mm,
            .y0 = by0 - stage_box_pad_mm - stage_label_h_mm,
            .x1 = bx1 + stage_box_pad_mm,
            .y1 = by1 + stage_box_pad_mm,
            .label = if (first.sec.len > 0) first.sec else seed_box_label,
            .members = members.items,
        });
    }
    return .{ .boxes = boxes.items, .dx = dx, .dy = dy };
}

/// Build the `sub_circuits` JSON array advertised in the dry-run response: one
/// entry per group whose freshly-added parts a saved layout can seed — counted
/// through the SAME source the Confirm path will use (`subCircuitSource`): a
/// sub-block reads its OWN starred module layout (`lib/modules/<m>.layouts.json`,
/// keyed by `origin_key`), a section reads the whole-design cache (keyed by
/// ref-des). So a design whose sub-modules are starred lists them even when it
/// has no whole-design layout of its own — previously this required a
/// whole-design layout and silently dropped every module-seedable sub-block.
/// Each entry carries the group `name`, the seedable `parts` count, the chosen
/// `anchor` ref (or null), and whether that anchor is already `on_board`. A
/// group with no seedable source is skipped. Computed from the still-populated
/// `pending_adds` after `emitStagedAdds`. Result lives on `arena`.
fn buildSubCircuitsJson(arena: std.mem.Allocator, d: *DiffContext) ![]const u8 {
    var buf: std.ArrayList(u8) = .empty;
    const w = buf.writer(arena);
    try w.writeByte('[');
    const adds = d.pending_adds.items;
    var buckets = std.StringHashMapUnmanaged(std.ArrayList(usize)).empty;
    var order: std.ArrayList([]const u8) = .empty;
    try groupAddsBySection(arena, adds, &buckets, &order);
    var emitted_first = true;
    for (order.items) |sec| {
        const idxs = (buckets.get(sec) orelse continue).items;
        // Resolve this group's pose source exactly as the seeder will, then
        // count how many of its adds that source names. No source (no module
        // layout and no whole-design cache) => nothing to seed => no checkbox.
        const src = (try subCircuitSource(d, sec)) orelse continue;
        var named: usize = 0;
        for (idxs) |idx| {
            const key = if (src.is_module) adds[idx].inst.origin_key else adds[idx].inst.ref_des;
            if (src.map.get(key) != null) named += 1;
        }
        if (named == 0) continue;
        // Module copper this group would carry onto the board (0 when the ★
        // snapshot has no routes, or when the source is the parent cache).
        const n_tracks = if (src.routes) |sr| sr.tracks.len else 0;
        const n_vias = if (src.routes) |sr| sr.vias.len else 0;
        const anchor = d.group_anchors.get(sec);
        if (!emitted_first) try w.writeByte(',');
        emitted_first = false;
        try w.writeAll("{\"name\":");
        try json_writer.writeString(w, sec);
        try w.print(",\"parts\":{d},\"tracks\":{d},\"vias\":{d},\"anchor\":", .{ named, n_tracks, n_vias });
        if (anchor) |a| {
            try json_writer.writeString(w, a.ref);
            try w.print(",\"on_board\":{}", .{a.on_board});
            // When the on-board anchor is bottom-side / rotated, the seed flips
            // the whole group to match — surface it so the Push modal can show it.
            if (a.on_board and a.board_back) try w.writeAll(",\"side\":\"bottom\"");
            if (a.on_board and a.board_rot != 0) try w.print(",\"rot\":{d}", .{a.board_rot});
        } else {
            try w.writeAll("null,\"on_board\":false");
        }
        try w.writeByte('}');
    }
    try w.writeByte(']');
    return buf.items;
}

/// Staging band for whole-sub-block seeds: blocks are packed left-to-right at
/// `BLOCK_STAGE_ORIGIN`, each preserving its module's internal arrangement, so a
/// not-yet-placed sub-block lands as a draggable unit clear of the main board.
const block_stage_origin_x_mm: f64 = 300.0;
const block_stage_origin_y_mm: f64 = 230.0;
const block_stage_gap_mm: f64 = 12.0;

/// Tier 1 of `emitStagedAdds`: group the buffered adds by their sub-block
/// (`pa.section` is the sub-block name for a sub-block part), and for each group
/// whose sub-block has a module default layout, place its parts as one unit in
/// the staging band using that layout. Parts not in a module-layout sub-block —
/// or a sub-block part the module layout doesn't name — are appended to
/// `leftover` for tiers 2–3. Only the *missing* parts are placed (already-placed
/// parts never reach `pending_adds`), so this fills a partly-placed block too.
fn seedSubBlocks(d: *DiffContext, w: anytype, f: *bool, add: []const PendingAdd, lo: *std.ArrayList(PendingAdd)) !void {
    const arena = d.spc.arena;
    if (d.sub_blocks.len == 0) {
        for (add) |pa| try lo.append(arena, pa);
        return;
    }
    // Group add indices by section, in a stable (sorted) block order.
    var buckets = std.StringHashMapUnmanaged(std.ArrayList(usize)).empty;
    var order: std.ArrayList([]const u8) = .empty;
    for (add, 0..) |pa, i| {
        const e = try buckets.getOrPut(arena, pa.section);
        if (!e.found_existing) {
            e.value_ptr.* = .empty;
            try order.append(arena, pa.section);
        }
        try e.value_ptr.append(arena, i);
    }
    std.mem.sort([]const u8, order.items, {}, lessThanStr);

    var cursor_x = block_stage_origin_x_mm;
    for (order.items) |sec| {
        const idxs = (buckets.get(sec) orelse continue).items;
        // Not a sub-block, or a sub-block with no module default layout → defer.
        const sb = subBlockNamed(d.sub_blocks, sec) orelse {
            for (idxs) |idx| try lo.append(arena, add[idx]);
            continue;
        };
        // Poses re-keyed onto this sub-block's own flattened refs (by origin_key)
        // so a module layout saved in a different design's ref-namespace still maps.
        const poses = pcb_layout.loadSubBlockPoses(arena, d.spc.project_dir, sb) orelse {
            for (idxs) |idx| try lo.append(arena, add[idx]);
            continue;
        };
        if (try seedOneSubBlock(d, w, f, sb, sec, add, idxs, poses, cursor_x, lo)) |advance|
            cursor_x += advance;
    }
}

/// Place one sub-block's missing parts at `cursor_x` in the staging band, using
/// `poses` (module-local). Returns the horizontal advance (block width + gap) to
/// pack the next block, or null when nothing was placed (no box drawn, cursor
/// unchanged). The module layout is keyed by the module's ref-des, which equals
/// the design's flattened sub-block ref minus the `<sec>/` prefix (both resolve
/// through the same instantiation), so a prefix-strip maps them.
fn seedOneSubBlock(
    d: *DiffContext,
    w: anytype,
    first: *bool,
    sb: env_mod.SubBlock,
    sec: []const u8,
    adds: []const PendingAdd,
    idxs: []const usize,
    poses: []const pcb_layout.RefPose,
    cursor_x: f64,
    leftover: *std.ArrayList(PendingAdd),
) !?f64 {
    const arena = d.spc.arena;
    var pmap = std.StringHashMapUnmanaged(pcb_layout.SyncPose).empty;
    var minx: f64 = std.math.floatMax(f64);
    var miny: f64 = std.math.floatMax(f64);
    var maxx: f64 = -std.math.floatMax(f64);
    var maxy: f64 = -std.math.floatMax(f64);
    for (poses) |p| {
        try pmap.put(arena, p.ref, .{ .x = p.x, .y = p.y, .rot = p.rot });
        minx = @min(minx, p.x);
        miny = @min(miny, p.y);
        maxx = @max(maxx, p.x);
        maxy = @max(maxy, p.y);
    }
    // Block-space → board-space translation: the block's min corner lands at
    // (cursor_x, BLOCK_STAGE_ORIGIN_Y), preserving the internal layout.
    const dx = cursor_x - minx;
    const dy = block_stage_origin_y_mm - miny;

    var seeded: usize = 0;
    for (idxs) |idx| {
        const pa = adds[idx];
        const local = stripSubPrefix(pa.inst.ref_des, sec);
        const pose = pmap.get(local) orelse {
            try leftover.append(arena, pa);
            continue;
        };
        const kmod = loadKicadMod(d.spc, pa.fp_name, pa.inst.component) orelse continue;
        const fp_def = loadFootprintDefForInstance(d.spc, pa.fp_name, pa.inst, pa.canopy_net, pa.section);
        try emitAddOp(
            w,
            first,
            pa.inst,
            pa.fp_name,
            kmod,
            fp_def,
            d.pad_net_map,
            .{
                .x_nm = mmToNm(pose.x + dx),
                .y_nm = mmToNm(pose.y + dy),
                .rot_deg = pose.rot,
                .back = pose.side == .bottom,
            },
            pa.canopy_net,
            pa.section,
        );
        d.summary.added += 1;
        seeded += 1;
    }
    if (seeded == 0) return null;
    d.summary.blocks += 1;

    // Outline + label box around the block's footprint extent.
    const bx0 = cursor_x - stage_box_pad_mm;
    const by0 = block_stage_origin_y_mm - stage_box_pad_mm - stage_label_h_mm;
    const bx1 = cursor_x + (maxx - minx) + stage_box_pad_mm;
    const by1 = block_stage_origin_y_mm + (maxy - miny) + stage_box_pad_mm;
    const items = [_][]const u8{
        try boardRectItem(arena, bx0, by0, bx1, by1),
        try boardLabelItem(arena, (bx0 + bx1) * stage_half, by0 + stage_label_h_mm * stage_half, sec),
    };
    for (items) |item_json| {
        if (!first.*) try w.*.writeAll(",");
        first.* = false;
        try w.*.writeAll(board_item_op_open);
        try w.*.writeAll(item_json);
        try w.*.writeAll("}");
    }

    // GND vias only on a fresh board AND when the WHOLE block is fresh (every
    // module part was a fresh add). On a populated board the staged block gets
    // dragged into place, but vias don't travel with footprints — they'd stay
    // stranded in the staging band. A partly-placed block's other parts live
    // elsewhere on the board, so block-frame vias for them would strand too.
    if (d.emit_layout_vias and d.board_fresh and seeded == poses.len)
        try emitBlockVias(d, w, first, sb.block, poses, dx, dy);

    return (bx1 - bx0) + block_stage_gap_mm;
}

/// Emit the GND vias for a fully-fresh seeded sub-block: route its module layout
/// in the block's own frame (`viasForPoses` over the sub-block's scoped block),
/// then offset each via by (dx, dy) so it lands under the staged parts. Net
/// names collapse the same way pad nets do. The writer de-dups by position.
fn emitBlockVias(d: *DiffContext, w: anytype, first: *bool, block: *const env_mod.DesignBlock, poses: []const pcb_layout.RefPose, dx: f64, dy: f64) !void {
    const vias = pcb_layout.viasForPoses(d.spc.arena, block, d.spc.project_dir, poses, .{}) orelse return;
    for (vias) |v| {
        const net = bareNetName(maybeCollapseDotSubNet(v.net, d.dot_nets));
        if (net.len == 0) continue;
        if (!first.*) try w.*.writeAll(",");
        first.* = false;
        try w.*.writeAll(add_via_op_open);
        try json_writer.writeString(w.*, net);
        try w.*.print(via_coords_fmt, .{
            mmToNm(v.x + dx), mmToNm(v.y + dy), mmToNm(v.dia), mmToNm(v.drill),
        });
        d.summary.vias += 1;
    }
}

/// The sub-block named `name`, or null when none matches (e.g. a design-`section`
/// part rather than a `(sub-block …)`).
fn subBlockNamed(sub_blocks: []const env_mod.SubBlock, name: []const u8) ?env_mod.SubBlock {
    for (sub_blocks) |sb| {
        if (std.mem.eql(u8, sb.name, name)) return sb;
    }
    return null;
}

/// Strip a leading `"<sec>/"` from a flattened ref-des, giving the module-local
/// ref the module layout is keyed by (`"buck_3v3d/R28"`, `"buck_3v3d"` → `"R28"`).
/// Returns `ref` unchanged when the prefix doesn't match.
fn stripSubPrefix(ref: []const u8, sec: []const u8) []const u8 {
    if (ref.len > sec.len + 1 and std.mem.startsWith(u8, ref, sec) and ref[sec.len] == '/')
        return ref[sec.len + 1 ..];
    return ref;
}

/// Emit `add_via` ops for the GND-plane stitching vias of a first-insertion
/// seed. Only fires when (a) vias weren't opted out (`?no_layout_vias=1`),
/// (b) the target board is FRESH (zero footprints — see
/// `DiffContext.board_fresh`; on a populated board the layout's via plan
/// lands on top of real routing), (c) the design has a premade layout,
/// (d) this sync actually added parts, and (e) **every** buffered add is
/// named by that layout — i.e. no part fell back to the staging grid. The
/// all-named gate matters because `loadSyncVias` rebuilds the *whole*
/// placement at the layout poses; a part the layout doesn't name would sit
/// at the origin there and drop a stray via, so we skip vias entirely for a
/// partial seed. The writer de-dups by position, so a later re-seed of the
/// same board re-emits these harmlessly. Net names are collapsed the same
/// way pad nets are, so the via lands on the GND the pads reference.
fn emitLayoutVias(
    d: *DiffContext,
    block: *const env_mod.DesignBlock,
    project_dir: []const u8,
    name: []const u8,
    parsed: ParsedSyncPlan,
    w: anytype,
    first: *bool,
) !void {
    if (!parsed.emit_layout_vias) return;
    if (!d.board_fresh) return;
    if (d.summary.added == 0) return;
    const layout = d.premade_layout orelse return;
    for (d.pending_adds.items) |pa| {
        if (!layout.contains(pa.inst.ref_des)) return; // partial seed → skip vias
    }
    const vias = pcb_layout.loadSyncVias(d.spc.arena, block, project_dir, name) orelse return;
    for (vias) |v| {
        const net = bareNetName(maybeCollapseDotSubNet(v.net, parsed.dot_nets));
        if (net.len == 0) continue;
        if (!first.*) try w.*.writeAll(",");
        first.* = false;
        try w.*.writeAll(add_via_op_open);
        try json_writer.writeString(w.*, net);
        try w.*.print(via_coords_fmt, .{
            mmToNm(v.x), mmToNm(v.y), mmToNm(v.dia), mmToNm(v.drill),
        });
        d.summary.vias += 1;
    }
}

/// Lay `adds` out as per-section clusters in a staging area and emit them: one
/// positioned `add` op per part plus a `create_board_item` box rect + text
/// label per section. Positions come from the diff-independent `staging_layout`
/// (built from the whole design), so a part lands at the same coordinate on
/// every push — a smaller follow-up push reuses the same cells and box size
/// instead of re-flowing fresh boxes over the previous push's. Parts with no
/// footprint source are skipped, and a section whose parts are all skipped
/// draws no (empty) box.
fn emitStagingGrid(d: *DiffContext, w: anytype, first: *bool, adds: []const PendingAdd) !void {
    const arena = d.spc.arena;
    if (adds.len == 0) return;

    var buckets = std.StringHashMapUnmanaged(std.ArrayList(usize)).empty;
    var order: std.ArrayList([]const u8) = .empty;
    try groupAddsBySection(arena, adds, &buckets, &order);

    // Uniform box size from the largest section across the *whole design* (not
    // just this push), so the box dimensions are identical on every sync.
    const ucols = boxCols(d.staging_layout.max_n);
    const urows = (d.staging_layout.max_n + ucols - 1) / ucols;
    const box_w = 2 * stage_box_pad_mm + @as(f64, @floatFromInt(ucols)) * stage_part_pitch_mm;
    const box_h = stage_label_h_mm + 2 * stage_box_pad_mm + @as(f64, @floatFromInt(urows)) * stage_part_pitch_mm;
    const pitch_x = box_w + stage_box_gap_x_mm;
    const pitch_y = box_h + stage_box_gap_y_mm;

    for (order.items) |sec| {
        const idxs = (buckets.get(sec) orelse continue).items;
        if (idxs.len == 0) continue;
        // Each section owns a fixed grid cell (`gi`), shared by all its parts.
        const gi = (d.staging_layout.by_ref.get(adds[idxs[0]].inst.ref_des) orelse StagingSlot{ .gi = 0, .slot = 0 }).gi;
        const cell_x = stage_origin_x_mm + @as(f64, @floatFromInt(gi % stage_sections_per_row)) * pitch_x;
        const cell_y = stage_origin_y_mm + @as(f64, @floatFromInt(gi / stage_sections_per_row)) * pitch_y;

        var emitted: usize = 0;
        for (idxs) |idx| {
            const pa = adds[idx];
            const kmod = loadKicadMod(d.spc, pa.fp_name, pa.inst.component) orelse continue;
            const fp_def = loadFootprintDefForInstance(d.spc, pa.fp_name, pa.inst, pa.canopy_net, pa.section);
            const slot = (d.staging_layout.by_ref.get(pa.inst.ref_des) orelse StagingSlot{ .gi = 0, .slot = 0 }).slot;
            const col: f64 = @floatFromInt(slot % ucols);
            const row: f64 = @floatFromInt(slot / ucols);
            const px = cell_x + stage_box_pad_mm + (col + stage_half) * stage_part_pitch_mm;
            const py = cell_y + stage_label_h_mm + stage_box_pad_mm + (row + stage_half) * stage_part_pitch_mm;
            try emitAddOp(w, first, pa.inst, pa.fp_name, kmod, fp_def, d.pad_net_map, .{ .x_nm = mmToNm(px), .y_nm = mmToNm(py) }, pa.canopy_net, pa.section);
            d.summary.added += 1;
            emitted += 1;
        }
        if (emitted > 0) {
            const items = [_][]const u8{
                try boardRectItem(arena, cell_x, cell_y, cell_x + box_w, cell_y + box_h),
                try boardLabelItem(arena, cell_x + box_w * stage_half, cell_y + stage_label_h_mm * stage_half, sec),
            };
            for (items) |item_json| {
                if (!first.*) try w.*.writeAll(",");
                first.* = false;
                try w.*.writeAll(board_item_op_open);
                try w.*.writeAll(item_json);
                try w.*.writeAll("}");
            }
        }
    }
}

