    reason: []const u8,
) !void {
    if (!first.*) try w.*.writeAll(",");
    first.* = false;
    try w.*.writeAll("{\"op\":\"swap_footprint\",\"uuid\":");
    try json_writer.writeString(w.*, uuid);
    // Display metadata for the preview modal (the writer targets by uuid).
    try w.*.writeAll(",\"ref\":");
    try json_writer.writeString(w.*, ref_des);
